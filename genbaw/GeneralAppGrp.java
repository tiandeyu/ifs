package ifs.genbaw;
//-----------------------------------------------------------------------------
//-----------------------------   Import def  ------------------------------
//-----------------------------------------------------------------------------

import ifs.fnd.asp.*;
import ifs.fnd.buffer.*;
import ifs.fnd.service.*;
import ifs.fnd.*;

//-----------------------------------------------------------------------------
//-----------------------------   Class def  ------------------------------
//-----------------------------------------------------------------------------

public class GeneralAppGrp extends ASPPageProvider
{

   //-----------------------------------------------------------------------------
   //---------- Static constants ------------------------------------------------
   //-----------------------------------------------------------------------------

   public static boolean DEBUG = Util.isDebugEnabled("ifs.genbaw.GeneralAppGrp");

   //-----------------------------------------------------------------------------
   //---------- Header Instances created on page creation --------
   //-----------------------------------------------------------------------------

   private ASPBlock headblk;
   private ASPRowSet headset;
   private ASPCommandBar headbar;
   private ASPTable headtbl;
   private ASPBlockLayout headlay;

   //-----------------------------------------------------------------------------
   //---------- Item Instances created on page creation --------
   //-----------------------------------------------------------------------------

   private ASPBlock general_app_grp_person_blk;
   private ASPRowSet general_app_grp_person_set;
   private ASPCommandBar general_app_grp_person_bar;
   private ASPTable general_app_grp_person_tbl;
   private ASPBlockLayout general_app_grp_person_lay;


   //-----------------------------------------------------------------------------
   //------------------------  Construction  ---------------------------
   //-----------------------------------------------------------------------------

   public  GeneralAppGrp (ASPManager mgr, String page_path)
   {
      super(mgr, page_path);
   }

   public void run() throws FndException
   {
      super.run();

      ASPManager mgr = getASPManager();

      if( mgr.commandBarActivated() )
         eval(mgr.commandBarFunction());
      else if(mgr.dataTransfered())
         okFind();
      else if( !mgr.isEmpty(mgr.getQueryStringValue("SEARCH")) )
         okFind();
      else if( !mgr.isEmpty(mgr.getQueryStringValue("GROUP_NO")) )
         okFind();
      adjust();
   }
   //-----------------------------------------------------------------------------
   //------------------------  Command Bar functions  ---------------------------
   //-----------------------------------------------------------------------------

   public void okFind()
   {
      ASPManager mgr = getASPManager();
      ASPTransactionBuffer trans = mgr.newASPTransactionBuffer();
      ASPQuery q;

      mgr.createSearchURL(headblk);
      q = trans.addQuery(headblk);
      q.includeMeta("ALL");
      if(mgr.dataTransfered())
         q.addOrCondition(mgr.getTransferedData());
      
      q.setOrderByClause("GROUP_SEQ");
      
      mgr.querySubmit(trans,headblk);
      if (  headset.countRows() == 0 )
      {
         mgr.showAlert("GENERALAPPGRPNODATA: No data found.");
         headset.clear();
      }
      okFindITEM1();
   }

   public void countFind()
   {
      ASPManager mgr = getASPManager();
      ASPTransactionBuffer trans = mgr.newASPTransactionBuffer();
      ASPQuery q;

      q = trans.addQuery(headblk);
      q.setSelectList("to_char(count(*)) N");
      mgr.submit(trans);
      headlay.setCountValue(toInt(headset.getValue("N")));
      headset.clear();
   }

   public void newRow()
   {
      ASPManager mgr = getASPManager();
      ASPTransactionBuffer trans = mgr.newASPTransactionBuffer();
      ASPBuffer data;
      ASPCommand cmd;

      cmd = trans.addEmptyCommand("HEAD","GENERAL_APP_GRP_API.New__",headblk);
      cmd.setOption("ACTION","PREPARE");
      trans = mgr.perform(trans);
      data = trans.getBuffer("HEAD/DATA");
      headset.addRow(data);
   }


   //-----------------------------------------------------------------------------
   //------------------------  Item block cmd bar functions  ---------------------------
   //-----------------------------------------------------------------------------


   public void okFindITEM1()
   {
      ASPManager mgr = getASPManager();
      
      if (headset.countRows() == 0)
         return;
      
      ASPTransactionBuffer trans = mgr.newASPTransactionBuffer();
      ASPQuery q;
      int headrowno;

      q = trans.addQuery(general_app_grp_person_blk);
      q.addWhereCondition("GROUP_NO = ?");
      q.addParameter("GROUP_NO", headset.getValue("GROUP_NO"));
      
      q.setOrderByClause("PERSON_SEQ");
      
      q.includeMeta("ALL");
      headrowno = headset.getCurrentRowNo();
      mgr.querySubmit(trans,general_app_grp_person_blk);
      headset.goTo(headrowno);
   }
   
   public void newRowITEM1()
   {
      ASPManager mgr = getASPManager();
      ASPTransactionBuffer trans = mgr.newASPTransactionBuffer();
      ASPCommand cmd;
      ASPBuffer data;

      
      cmd = trans.addEmptyCommand("ITEM1","GENERAL_APP_GRP_PERSON_API.New__",general_app_grp_person_blk);
      cmd.setOption("ACTION","PREPARE");
      cmd.setParameter("ITEM0_GROUP_NO", headset.getValue("GROUP_NO"));
      trans = mgr.perform(trans);
      data = trans.getBuffer("ITEM1/DATA");
      general_app_grp_person_set.addRow(data);
   }

   //-----------------------------------------------------------------------------
   //------------------------  Predefines Head ---------------------------
   //-----------------------------------------------------------------------------

   public void preDefine()
   {
      ASPManager mgr = getASPManager();
      
      headblk = mgr.newASPBlock("MAIN");
      headblk.addField("OBJID").
      setHidden();
      
      headblk.addField("OBJVERSION").
      setHidden();
      
      headblk.addField("GROUP_NO").
      setMandatory().
      setInsertable().
      setUpperCase().
      setLabel("GENERALAPPGRPGROUPNO: Group No").
      setSize(20);
      
      headblk.addField("GROUP_DESC").
      setInsertable().
      setLabel("GENERALAPPGRPGROUPDESC: Group Desc").
      setSize(20);
      
      headblk.addField("GROUP_SEQ","Number").
      setInsertable().
      setLabel("GENERALAPPGRPGROUPSEQ: Group Seq").
      setSize(10);
      
      headblk.addField("NOTE").
      setInsertable().
      setLabel("GENERALAPPGRPNOTE: Note").
      setSize(50);
      
      headblk.setView("GENERAL_APP_GRP");
      headblk.defineCommand("GENERAL_APP_GRP_API", "New__,Modify__,Remove__");
      headset = headblk.getASPRowSet();
      headbar = mgr.newASPCommandBar(headblk);
      headlay = headblk.getASPBlockLayout();
      headlay.setDefaultLayoutMode(headlay.SINGLE_LAYOUT);
//      headlay.setFieldOrder("NOTE,GROUP_SEQ");
      headtbl = mgr.newASPTable(headblk);
      headtbl.setTitle("GENERALAPPGRPTBLHEAD: General App Grps");
      headtbl.enableRowSelect();
      headtbl.setWrap();
      
      
      //
      // General Approve Group Persons
      //
      
      general_app_grp_person_blk = mgr.newASPBlock("ITEM1");
      general_app_grp_person_blk.addField("ITEM0_OBJID").
      setHidden().
      setDbName("OBJID");
      
      general_app_grp_person_blk.addField("ITEM0_OBJVERSION").
      setHidden().
      setDbName("OBJVERSION");
      
      general_app_grp_person_blk.addField("ITEM0_GROUP_NO").
      setDbName("GROUP_NO").
      setMandatory().
      setInsertable().
      setHidden().
      setLabel("GENERALAPPGRPPERSONITEM0GROUPNO: Group No").
      setSize(20);
      
      general_app_grp_person_blk.addField("PERSON_ID").
      setMandatory().
      setInsertable().
      setUpperCase().
      setDynamicLOV("PERSON_INFO_LOV").
      setLOVProperty("WHERE", "USER_ID IS NOT NULL").
      setLabel("GENERALAPPGRPPERSONPERSONID: Person Id").
      setSize(20);
      
      general_app_grp_person_blk.addField("PERSON_NAME").
      setFunction("Person_Info_API.Get_Name(:PERSON_ID)").
      setReadOnly().
      setLabel("GENERALAPPGRPPERSONPERSONNAME: Person Name").
      setSize(20);
      mgr.getASPField("PERSON_ID").setValidation("PERSON_NAME");
      
      general_app_grp_person_blk.addField("PERSON_SEQ", "Number").
      setInsertable().
      setLabel("GENERALAPPGRPPERSONPERSONSEQ: Person Seq").
      setSize(10);
      
      general_app_grp_person_blk.addField("ITEM0_NOTE").
      setDbName("NOTE").
      setInsertable().
      setLabel("GENERALAPPGRPPERSONITEM0NOTE: Note").
      setSize(50);
      
      general_app_grp_person_blk.setView("GENERAL_APP_GRP_PERSON");
      general_app_grp_person_blk.defineCommand("GENERAL_APP_GRP_PERSON_API","New__,Modify__,Remove__");
      general_app_grp_person_blk.setMasterBlock(headblk);
      general_app_grp_person_set = general_app_grp_person_blk.getASPRowSet();
      general_app_grp_person_bar = mgr.newASPCommandBar(general_app_grp_person_blk);
      general_app_grp_person_bar.defineCommand(general_app_grp_person_bar.OKFIND, "okFindITEM1");
      general_app_grp_person_bar.defineCommand(general_app_grp_person_bar.NEWROW, "newRowITEM1");
      general_app_grp_person_tbl = mgr.newASPTable(general_app_grp_person_blk);
      general_app_grp_person_tbl.setTitle("GENERALAPPGRPPERSONITEMHEAD1: GeneralAppGrpPerson");
      general_app_grp_person_tbl.enableRowSelect();
      general_app_grp_person_tbl.setWrap();
      general_app_grp_person_lay = general_app_grp_person_blk.getASPBlockLayout();
      general_app_grp_person_lay.setDefaultLayoutMode(general_app_grp_person_lay.MULTIROW_LAYOUT);
      
      general_app_grp_person_lay.setSimple("PERSON_NAME");
   }


   public void adjust() throws FndException
   {
      super.adjust();
   }

   //-----------------------------------------------------------------------------
   //------------------------  Presentation functions  ---------------------------
   //-----------------------------------------------------------------------------

   protected String getDescription()
   {
      return "GENERALAPPGRPDESC: General App Grp";
   }

   protected String getTitle()
   {
      return getDescription();
   }

   protected void printContents() throws FndException
   {
      super.printContents();

      ASPManager mgr = getASPManager();
      if (!headlay.isVisible())
         headlay.setLayoutMode(headlay.CUSTOM_LAYOUT);
      appendToHTML(headlay.show());
         
      if (headset.countRows() > 0 && (headlay.isSingleLayout() || headlay.isCustomLayout()))
         appendToHTML(general_app_grp_person_lay.show());
   }
}
