   public  SchPlanWorkNav (ASPManager mgr, String page_path)
   {
      super(mgr,page_path);
   }

   public void run()
   {
      ASPManager mgr = getASPManager();
      bEmptyTree = false;
      okFind();
      if( mgr.commandBarActivated() ){
         comnd = mgr.readValue("__COMMAND");         
         if  ( "HEAD.Find".equals(comnd) )
         bEmptyTree = true;   // Clear tree when in the find layout
         eval(mgr.commandBarFunction());
         }
      else if(mgr.dataTransfered())
         okFind();
      else if( !mgr.isEmpty(mgr.getQueryStringValue("SEARCH")) )
         okFind();
      else if( !mgr.isEmpty(mgr.getQueryStringValue("WORK_NO")) )
         okFind();
      adjust();
   }
   //-----------------------------------------------------------------------------
   //------------------------  Command Bar functions  ---------------------------
   //-----------------------------------------------------------------------------

   public void okFind()
   {
      ASPManager mgr = getASPManager();
      ASPTransactionBuffer trans = mgr.newASPTransactionBuffer();
      ASPQuery q;

      mgr.createSearchURL(headblk);
      q = trans.addQuery(headblk);
      q.addWhereCondition("PARENT_ID='001'");
      q.addOrderByClause("PROJ_NO,ID");
      q.includeMeta("ALL");
      if(mgr.dataTransfered())
         q.addOrCondition(mgr.getTransferedData());
      mgr.querySubmit(trans,headblk);
      if (  headset.countRows() == 0 )
      {
         mgr.showAlert("SCHPLANWORKNAVNODATA: No data found.");
         headset.clear();
      }
   }



   public void countFind()
   {
      ASPManager mgr = getASPManager();
      ASPTransactionBuffer trans = mgr.newASPTransactionBuffer();
      ASPQuery q;

      q = trans.addQuery(headblk);
      q.setSelectList("to_char(count(*)) N");
      mgr.submit(trans);
      headlay.setCountValue(toInt(headset.getValue("N")));
      headset.clear();
   }



   public void newRow()
   {
      ASPManager mgr = getASPManager();
      ASPContext ctx =  mgr.getASPContext();
      ASPTransactionBuffer trans = mgr.newASPTransactionBuffer();
      ASPBuffer data;
      ASPCommand cmd;

      cmd = trans.addEmptyCommand("HEAD","SCH_PLAN_WBS_API.New__",headblk);
      cmd.setParameter("PROJ_NO", ctx.findGlobal(GenbawConstants.PERSON_DEFAULT_PROJECT));
      cmd.setParameter("PARENT_ID", "001");
      cmd.setOption("ACTION","PREPARE");
      trans = mgr.perform(trans);
      data = trans.getBuffer("HEAD/DATA");
      headset.addRow(data);
   }


   //-----------------------------------------------------------------------------
   //------------------------  Predefines Head ---------------------------
   //-----------------------------------------------------------------------------

   public void  preDefine()
   {
      ASPManager mgr = getASPManager();

      headblk = mgr.newASPBlock("MAIN");
      headblk.addField("OBJID").
              setHidden();
      headblk.addField("OBJVERSION").
              setHidden();
      headblk.addField("PROJ_NO").
              setMandatory().
              setReadOnly().
              setDynamicLOV("GENERAL_PROJECT").
              setLabel("SCHPLANWORKNAVPROJNO: Proj No").
              setSize(40);
     
      headblk.addField("PROJ_DESC").
              setFunction("GENERAL_PROJECT_API.Get_Proj_Desc(:PROJ_NO)").
              setReadOnly().   
              setLabel("PROJECTBUDGETPROJDESC: Proj Desc").
              setSize(40);  
      mgr.getASPField("PROJ_NO").setValidation("PROJ_DESC");
      headblk.addField("ID").
              setReadOnly().   
              setLabel("SCHPLANWORKNAVID: Eps No").
              setSize(40);
      headblk.addField("WBS_NO").
              setMandatory().
              setReadOnly().
              setLabel("SCHPLANWORKNAVWBSNO: Wbs_No").
              setSize(40);
      headblk.addField("WBS_NAME").
              setInsertable().
              setFunction("SCH_EPS_API.Get_Eps_Name (:PROJ_NO,:ID,:REV)").
              setLabel("SCHPLANWORKNAVWBSNAME: Wbs Name").
              setSize(40);
      mgr.getASPField("ID").setValidation("WBS_NAME");
      headblk.addField("REV").
               setMandatory().
               setReadOnly().
               setLabel("SCHPLANWORKNAVREV: Rev").
               setSize(40); 
       
      headblk.setView("SCH_PLAN_WBS");
      headblk.defineCommand("SCH_PLAN_WBS_API","");
      headset = headblk.getASPRowSet();  
      headbar = mgr.newASPCommandBar(headblk);
      headtbl = mgr.newASPTable(headblk);
      headtbl.setTitle("SCHPLANWORKNAVTBLHEAD: Sch Plan Work Nav");
      headtbl.enableRowSelect();
      headtbl.setWrap();
      headlay = headblk.getASPBlockLayout();
      headlay.setDefaultLayoutMode(headlay.MULTIROW_LAYOUT);
      headlay.setSimple("PROJ_DESC");
        
   }



   public void  adjust()
   {
      ASPManager mgr = getASPManager();
      // fill function body
      if (( bEmptyTree )|| ( headset.countRows() == 0 ))
      {
         src_str    = "SchPlanWorkTree.page";
         iframe_src = "SchPlanWorkBlank.page";
      }
      else        
      {    
         headbar.disableCommand(headbar.NEWROW);   
         wbs_no    = headset.getValue("WBS_NO");   
         proj_no = headset.getValue("PROJ_NO");       
         src_str    = "SchPlanWorkTree.page?WBS_NO="+wbs_no+"&PROJ_NO=" + proj_no+"&ID=" + headset.getValue("ID")+"&REV=" + headset.getValue("REV")+"&WBS_NAME=" + mgr.URLEncode(headset.getValue("WBS_NAME")) ;
         iframe_src = "SchPlanWork.page?WBS_NO="+wbs_no+"&PROJ_NO=" + proj_no+"&ID=" + headset.getValue("ID")+"&REV=" + headset.getValue("REV")+"&WBS_NAME=" + mgr.URLEncode(headset.getValue("WBS_NAME"));
      }               
   }            

