   public void okFind()
   {
      ASPManager mgr = getASPManager();
      ASPTransactionBuffer trans = mgr.newASPTransactionBuffer();
      ASPQuery q;

      mgr.createSearchURL(headblk);
      q = trans.addQuery(headblk);
      q.addWhereCondition("PARENT_ID='001'");
      q.includeMeta("ALL");
      if(mgr.dataTransfered())
         q.addOrCondition(mgr.getTransferedData());
      mgr.querySubmit(trans,headblk);
      if (  headset.countRows() == 0 )
      {
         mgr.showAlert("SCHPLANWBSNODATA: No data found.");
         headset.clear();
      }
   }



   public void countFind()
   {
      ASPManager mgr = getASPManager();
      ASPTransactionBuffer trans = mgr.newASPTransactionBuffer();
      ASPQuery q;

      q = trans.addQuery(headblk);
      q.setSelectList("to_char(count(*)) N");
      mgr.submit(trans);
      headlay.setCountValue(toInt(headset.getValue("N")));
      headset.clear();
   }



   public void newRow()
   {
      ASPManager mgr = getASPManager();
      ASPContext ctx =  mgr.getASPContext();
      ASPTransactionBuffer trans = mgr.newASPTransactionBuffer();
      ASPTransactionBuffer trans1 = mgr.newASPTransactionBuffer();
      ASPBuffer data;
      ASPCommand cmd;
      
      cmd = trans1.addCustomFunction("GET_PROJ_DESC","GENERAL_PROJECT_API.GET_PROJ_DESC","PROJ_DESC");
      cmd.addParameter("PROJ_NO",ctx.findGlobal("PROJ_NO"));

      trans1 = mgr.validate(trans1);
      String PROJ_DESC= trans1.getValue("GET_PROJ_DESC/DATA/PROJ_DESC");

      cmd = trans.addEmptyCommand("HEAD","SCH_PLAN_WBS_API.New__",headblk);
      cmd.setParameter("PROJ_NO", ctx.findGlobal(GenbawConstants.PERSON_DEFAULT_PROJECT));
      cmd.setParameter("PARENT_ID", "001");
      cmd.setParameter("PROJ_DESC", PROJ_DESC);
      cmd.setParameter("REV", "A");
      cmd.setOption("ACTION","PREPARE");
      trans = mgr.perform(trans);
      data = trans.getBuffer("HEAD/DATA");
      headset.addRow(data);
   }
   
   public void validate() {
      // TODO Auto-generated method stub
      ASPManager mgr = getASPManager();
      ASPTransactionBuffer trans = mgr.newASPTransactionBuffer();
      ASPTransactionBuffer trans1 = mgr.newASPTransactionBuffer();
      ASPCommand cmd;
      String val = mgr.readValue("VALIDATE");
      String txt = "";
      String EPSNAME = "";
      String RESPONSEORG = "";
      String RESPONSEORGNAME = "";
     
      
      if("ID".equals(val)) {
         
         cmd = trans.addCustomFunction("GETEPSNAME", 
               "SCH_EPS_API.Get_Eps_Name", "WBS_NAME");
         cmd.addParameter("PROJ_NO,ID,REV");
         
         cmd = trans.addCustomFunction("GETRESPONSEORG", 
               "SCH_EPS_API.Get_Response_Org", "RESPONSE_ORG");
         cmd.addParameter("PROJ_NO,ID,REV");

         trans = mgr.validate(trans);   
                
         EPSNAME = trans.getValue("GETEPSNAME/DATA/WBS_NAME");
         RESPONSEORG = trans.getValue("GETRESPONSEORG/DATA/RESPONSE_ORG");
         
         cmd = trans1.addCustomFunction("GETRESPONSEORGNAME", 
               "GENERAL_ZONE_API.GET_ZONE_DESC", "RESPONSE_ORG_NAME");
         cmd.addParameter("RESPONSE_ORG",RESPONSEORG);
         
         trans1 = mgr.validate(trans1);
         
         RESPONSEORGNAME = trans1.getValue("GETRESPONSEORGNAME/DATA/RESPONSE_ORG_NAME");

         txt = ((mgr.isEmpty(EPSNAME)) ? "" : EPSNAME ) + "^" 
               + ((mgr.isEmpty(RESPONSEORG)) ? "" : RESPONSEORG ) + "^"
               + ((mgr.isEmpty(RESPONSEORGNAME)) ? "" : RESPONSEORGNAME ) + "^";
         
         mgr.responseWrite(txt);
      }
     mgr.endResponse();
      
   }


