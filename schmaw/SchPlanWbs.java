   public void  adjust()
   {
      ASPManager mgr = getASPManager();
      // fill function body
      if (( bEmptyTree )|| ( headset.countRows() == 0 ))
      {
         src_str    = "SchPlanWbsTree.page";
         iframe_src = "SchPlanWbsBlank.page";
      }
      else        
      {         
         wbs_no    = headset.getValue("WBS_NO");   
         proj_no = headset.getValue("PROJ_NO");   
         src_str    = "SchPlanWbsTree.page?WBS_NO="+wbs_no+"&PROJ_NO=" + proj_no+"&ID=" + headset.getValue("ID")+"&WBS_NAME=" + mgr.URLEncode(headset.getValue("WBS_NAME"))+"&REV=" + mgr.URLEncode(headset.getValue("REV")); 
         iframe_src = "SchPlanWbsLine.page?WBS_NO="+wbs_no+"&PROJ_NO=" + proj_no+"&ID=" + headset.getValue("ID")+"&WBS_NAME=" + mgr.URLEncode(headset.getValue("WBS_NAME"))+"&REV=" + mgr.URLEncode(headset.getValue("REV"));
      }
      
      if("MAIN.SaveReturn".equals(mgr.readValue("__COMMAND"))){
         mgr.getASPField("WBS_NO").setHidden();
      }
      if("MAIN.OkFind".equals(mgr.readValue("__COMMAND"))){
         mgr.getASPField("WBS_NO").setHidden();
      }
   }

