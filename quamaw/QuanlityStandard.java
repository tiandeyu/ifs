   public  QuanlityStandard (ASPManager mgr, String page_path)
   {
      super(mgr,page_path);
   }

   public void run()
   {
      ASPManager mgr = getASPManager();
      bEmptyTree = false;

      if( mgr.commandBarActivated() )
      {
         comnd = mgr.readValue("__COMMAND");         
         if  ( "HEAD.Find".equals(comnd) )
            bEmptyTree = true;    
         eval(mgr.commandBarFunction());
      }         
      else if(mgr.dataTransfered())
         okFind();
      else if( !mgr.isEmpty(mgr.getQueryStringValue("SEARCH")) )
         okFind();
      else if( !mgr.isEmpty(mgr.getQueryStringValue("STANDARD_NO")) )
         okFind();
      else
         okFind();
      adjust();
   }
   //-----------------------------------------------------------------------------
   //------------------------  Command Bar functions  ---------------------------
   //-----------------------------------------------------------------------------

   public void okFind()
   {
      ASPManager mgr = getASPManager();
      ASPTransactionBuffer trans = mgr.newASPTransactionBuffer();
      ASPQuery q;

      mgr.createSearchURL(headblk);
      q = trans.addQuery(headblk);
      q.includeMeta("ALL");
      if(mgr.dataTransfered())
         q.addOrCondition(mgr.getTransferedData());
      mgr.querySubmit(trans,headblk);
      if (  headset.countRows() == 0 )
      {
         mgr.showAlert("QUANLITYSTANDARDNODATA: No data found.");
         headset.clear();
      }
   }



   public void countFind()
   {
      ASPManager mgr = getASPManager();
      ASPTransactionBuffer trans = mgr.newASPTransactionBuffer();
      ASPQuery q;

      q = trans.addQuery(headblk);
      q.setSelectList("to_char(count(*)) N");
      mgr.submit(trans);
      headlay.setCountValue(toInt(headset.getValue("N")));
      headset.clear();
   }



   public void newRow()
   {
      ASPManager mgr = getASPManager();
      ASPTransactionBuffer trans = mgr.newASPTransactionBuffer();
      ASPBuffer data;
      ASPCommand cmd;

      cmd = trans.addEmptyCommand("HEAD","QUANLITY_STANDARD_API.New__",headblk);
      cmd.setOption("ACTION","PREPARE");
      trans = mgr.perform(trans);
      data = trans.getBuffer("HEAD/DATA");
      headset.addRow(data);
   }


   //-----------------------------------------------------------------------------
   //------------------------  Predefines Head ---------------------------
   //-----------------------------------------------------------------------------

   public void  preDefine()
   {
      ASPManager mgr = getASPManager();

      headblk = mgr.newASPBlock("MAIN");
      headblk.addField("OBJID").
              setHidden();
      headblk.addField("OBJVERSION").
              setHidden();
      headblk.addField("PROJECT_TYPE_NO").
              setMandatory().
              setInsertable().
              setDynamicLOV("PROJECT_TYPE",600,450).
              setLabel("QUANLITYSTANDARDPROJECTTYPENO: Project Type No").
              setSize(20);
      headblk.addField("PROJECT_TYPE_NAME").
              setFunction("PROJECT_TYPE_API.Get_Project_Type_Name( :PROJECT_TYPE_NO)").
              setLabel("PROJECTTYPEPROJECTTYPENAME: Project Type Name").
              setSize(30).
              setReadOnly();
      mgr.getASPField("PROJECT_TYPE_NO").setValidation("PROJECT_TYPE_NAME");
      
      headblk.addField("STANDARD_NO").
              setMandatory().
              setInsertable().
              setDynamicLOV("QUA_CRITERION","PROJECT_TYPE_NO").
              setLabel("QUANLITYSTANDARDSTANDARDNO: Standard No").
              setSize(20);
      headblk.addField("STANDARD_DESC").
              setFunction("Qua_Criterion_Api.Get_Description(:PROJECT_TYPE_NO,:STANDARD_NO)").
              setLabel("QUANLITYSTANDARDSTANDARDDESC: Standard Desc").
              setReadOnly();      
      mgr.getASPField("STANDARD_NO").setValidation("STANDARD_DESC");
      headblk.setView("QUANLITY_STANDARD");
      headblk.defineCommand("QUANLITY_STANDARD_API","New__,Modify__,Remove__");
      headset = headblk.getASPRowSet();
      headbar = mgr.newASPCommandBar(headblk);
      headtbl = mgr.newASPTable(headblk);
      headtbl.setTitle("QUANLITYSTANDARDTBLHEAD: Quanlity Standards");
      headtbl.enableRowSelect();
      headtbl.setWrap();
      headlay = headblk.getASPBlockLayout();
      headlay.setDefaultLayoutMode(headlay.MULTIROW_LAYOUT);
 



   }



   public void  adjust()
   {
      // fill function body
      if (( bEmptyTree )|| ( headset.countRows() == 0 ))
      {
         src_str    = "QuanlityStandardTree.page";
      }
      else
      {
         standard_no    = headset.getValue("STANDARD_NO");
         project_type    = headset.getValue("PROJECT_TYPE_NO");    
         src_str    = "QuanlityStandardTree.page?STANDARD_NO="+standard_no+"&PROJECT_TYPE_NO="+project_type;
      }      
   }
   
   public String  modifiedClientScript(ASPManager mgr,String sGenerateClientScript)
   {
      sGenerateClientScript = mgr.replace(sGenerateClientScript,"document.location=","window.parent.location=");
      return sGenerateClientScript;


   }   

