package ifs.genfrw;
//-----------------------------------------------------------------------------
//-----------------------------   Import def  ------------------------------
//-----------------------------------------------------------------------------

import ifs.fnd.asp.*;
import ifs.fnd.buffer.*;
import ifs.fnd.service.*;
import ifs.fnd.*;

//-----------------------------------------------------------------------------
//-----------------------------   Class def  ------------------------------
//-----------------------------------------------------------------------------

public class GenFormTypeGroups extends ASPPageProvider
{

   //-----------------------------------------------------------------------------
   //---------- Static constants ------------------------------------------------
   //-----------------------------------------------------------------------------

   public static boolean DEBUG = Util.isDebugEnabled("ifs.genfrw.GenFormTypeGroups");

   //-----------------------------------------------------------------------------
   //---------- Header Instances created on page creation --------
   //-----------------------------------------------------------------------------

   private ASPBlock headblk;
   private ASPRowSet headset;
   private ASPCommandBar headbar;
   private ASPTable headtbl;
   private ASPBlockLayout headlay;

   //-----------------------------------------------------------------------------
   //------------------------  Construction  ---------------------------
   //-----------------------------------------------------------------------------

   public  GenFormTypeGroups (ASPManager mgr, String page_path)
   {
      super(mgr,page_path);
   }

   //-----------------------------------------------------------------------------
   //---------------------------  Run logic functions  ---------------------------
   //-----------------------------------------------------------------------------

   public void run() throws FndException
   {
      super.run();

      ASPManager mgr = getASPManager();

      if( mgr.commandBarActivated() )
         eval(mgr.commandBarFunction());
      else if(mgr.dataTransfered())
         okFind();
      else if( !mgr.isEmpty(mgr.getQueryStringValue("SEARCH")) )
         okFind();
      else if( !mgr.isEmpty(mgr.getQueryStringValue("TITLE")) )
         okFind();
      adjust();
   }

   //-----------------------------------------------------------------------------
   //------------------------  Command Bar functions  ---------------------------
   //-----------------------------------------------------------------------------

   public void okFind()
   {
      ASPManager mgr = getASPManager();
      ASPTransactionBuffer trans = mgr.newASPTransactionBuffer();
      ASPQuery q;

      mgr.createSearchURL(headblk);
      q = trans.addQuery(headblk);
      q.includeMeta("ALL");
      if(mgr.dataTransfered())
         q.addOrCondition(mgr.getTransferedData());
      mgr.querySubmit(trans,headblk);
      if (  headset.countRows() == 0 )
      {
         mgr.showAlert("GENFORMTYPEGROUPSNODATA: No data found.");
         headset.clear();
      }
   }

   public void countFind()
   {
      ASPManager mgr = getASPManager();
      ASPTransactionBuffer trans = mgr.newASPTransactionBuffer();
      ASPQuery q;

      q = trans.addQuery(headblk);
      q.setSelectList("to_char(count(*)) N");
      mgr.submit(trans);
      headlay.setCountValue(toInt(headset.getValue("N")));
      headset.clear();
   }

   public void newRow()
   {
      ASPManager mgr = getASPManager();
      ASPTransactionBuffer trans = mgr.newASPTransactionBuffer();
      ASPBuffer data;
      ASPCommand cmd;

      cmd = trans.addEmptyCommand("HEAD","GEN_FORM_TYPE_GROUPS_API.New__",headblk);
      cmd.setOption("ACTION","PREPARE");
      trans = mgr.perform(trans);
      data = trans.getBuffer("HEAD/DATA");
      headset.addRow(data);
   }


   //-----------------------------------------------------------------------------
   //------------------------  Predefines Head ---------------------------
   //-----------------------------------------------------------------------------

   public void  preDefine()
   {
      ASPManager mgr = getASPManager();

      headblk = mgr.newASPBlock("MAIN");
      headblk.addField("OBJID").
      setHidden();
      headblk.addField("OBJVERSION").
      setHidden();

      // line 1
      headblk.addField("TITLE").
      setMandatory().
      setInsertable().
      setLabel("GENFORMTYPEGROUPSTITLE: Title").
      setSize(20);
      
      headblk.addField("SHOW_HEADER").
      setInsertable().
      setCheckBox("FALSE,TRUE").
      setLabel("GENFORMTYPEGROUPSSHOWHEADER: Show Header").
      setSize(5);

      // line 2
      headblk.addField("SHOW_FIRST").
      setInsertable().
      setCheckBox("FALSE,TRUE").
      setLabel("GENFORMTYPEGROUPSSHOWFIRST: Show First").
      setSize(5);
      
      headblk.addField("SHOW_COLUMNS", "Number").
      setInsertable().
      setLabel("GENFORMTYPEGROUPSSHOWCOLUMNS: Show Columns").
      setSize(10);

      // line 3
      headblk.addField("SHOW_TITLE").
      setInsertable().
      setCheckBox("FALSE,TRUE").
      setLabel("GENFORMTYPEGROUPSSHOWTITLE: Show Title").
      setSize(5);
      
      headblk.addField("GROUP_ORDER","Number").
      setInsertable().
      setLabel("GENFORMTYPEGROUPSGROUPORDER: Group Order").
      setSize(10);

      headblk.setView("GEN_FORM_TYPE_GROUPS");
      headblk.defineCommand("GEN_FORM_TYPE_GROUPS_API", "New__,Modify__,Remove__");
      headset = headblk.getASPRowSet();
      headbar = mgr.newASPCommandBar(headblk);
      headtbl = mgr.newASPTable(headblk);
      headtbl.setTitle("GENFORMTYPEGROUPSTBLHEAD: Gen Form Type Groupss");
      headtbl.enableRowSelect();
      headtbl.setWrap();
      headlay = headblk.getASPBlockLayout();
      headlay.setDefaultLayoutMode(headlay.MULTIROW_LAYOUT);

   }



   public void  adjust()
   {
      
   }


   //-----------------------------------------------------------------------------
   //------------------------  Presentation functions  ---------------------------
   //-----------------------------------------------------------------------------

   protected String getDescription()
   {
      return "GENFORMTYPEGROUPSDESC: Gen Form Type Groups";
   }


   protected String getTitle()
   {
      return getDescription();
   }

   protected void printContents() throws FndException
   {
      ASPManager mgr = getASPManager();
      if (headlay.isVisible())
          appendToHTML(headlay.show());
   }
}
