   public void validate(){
      ASPManager mgr = getASPManager();
      ASPTransactionBuffer trans = mgr.newASPTransactionBuffer();
      ASPCommand cmd;
      String val = mgr.readValue("VALIDATE"); 
      String txt = "";
      String contractId = "";
      String contractDesc = ""; 
      String thirdSide = "";
      String thirdSideName = "";
      

      if ("ENTRY_ID".equals(val)) {   
         cmd = trans.addCustomFunction("CONTRACT_NO", 
               "MAT_ST_ENTRY_API.Get_Contract_Id", "CONTRACT_NO");
         cmd.addParameter("PROJ_NO,ENTRY_ID");
                        
         trans = mgr.validate(trans);
         contractId = trans.getValue("CONTRACT_NO/DATA/CONTRACT_NO");         
         trans.clear();
         cmd.clear();
                 
         cmd = trans.addCustomFunction("CONTRACT_DESC", 
               "PROJECT_CONTRACT_API.Get_Contract_Desc", "CONTRACT_DESC");
         cmd.addParameter("PROJ_NO");
         cmd.addParameter("CONTRACT_NO",contractId);
         
         cmd = trans.addCustomFunction("THIRD_SIDE", 
               "PROJECT_CONTRACT_API.Get_Third_Side", "THIRD_SIDE");
         cmd.addParameter("PROJ_NO");
         cmd.addParameter("CONTRACT_NO",contractId);
  
         trans = mgr.validate(trans);
         contractDesc = trans.getValue("CONTRACT_DESC/DATA/CONTRACT_DESC");
         thirdSide = trans.getValue("THIRD_SIDE/DATA/THIRD_SIDE");
         trans.clear();
         cmd.clear();
         
         cmd = trans.addCustomFunction("THIRD_SIDE_NAME", 
               "SUPPLIER_INFO_API.Get_Name", "THIRD_SIDE_NAME");
         cmd.addParameter("THIRD_SIDE",thirdSide);
         trans = mgr.validate(trans);
         thirdSideName = trans.getValue("THIRD_SIDE_NAME/DATA/THIRD_SIDE_NAME"); 
         
         txt = ((mgr.isEmpty(contractId)) ? "" : contractId) + "^" + ((mgr.isEmpty(contractDesc)) ? "" : contractDesc) + "^";
         txt = txt + ((mgr.isEmpty(thirdSide)) ? "" : thirdSide) + "^" + ((mgr.isEmpty(thirdSideName)) ? "" : thirdSideName) + "^";
         mgr.responseWrite(txt);
                 
      }

      if ("ENTRY_LINE_NO".equals(val)) {
         cmd = trans.addCustomFunction("ITEM_NO","MAT_ST_ENTRY_LINE_API.Get_Item_No","ITEM_NO");
         cmd.addParameter("ITEM0_PROJ_NO");
         cmd.addParameter("ENTRY_ID");
         cmd.addParameter("ENTRY_LINE_NO");
         cmd = trans.addCustomFunction("ITEM0_ENTRY_QTY","MAT_ST_ENTRY_LINE_API.Get_Entry_Qty","ITEM0_ENTRY_QTY");
         cmd.addParameter("ITEM0_PROJ_NO");
         cmd.addParameter("ENTRY_ID");
         cmd.addParameter("ENTRY_LINE_NO");
         cmd = trans.addCustomFunction("ITEM0_UNIT_NO","MAT_ST_ENTRY_LINE_API.Get_Unit_No","ITEM0_UNIT_NO");
         cmd.addParameter("ITEM0_PROJ_NO");
         cmd.addParameter("ENTRY_ID");
         cmd.addParameter("ENTRY_LINE_NO");
         cmd = trans.addCustomFunction("ITEM0_TOTLE_PRICE","MAT_ST_ENTRY_LINE_API.Get_Totle_Price","ITEM0_TOTLE_PRICE");
         cmd.addParameter("ITEM0_PROJ_NO");
         cmd.addParameter("ENTRY_ID");
         cmd.addParameter("ENTRY_LINE_NO");
         cmd = trans.addCustomFunction("CONTRACT_NO", "MAT_ST_ENTRY_API.Get_Contract_Id", "CONTRACT_NO");
         cmd.addParameter("ITEM0_PROJ_NO,ENTRY_ID");
                                              
         trans = mgr.validate(trans);
         contractId = trans.getValue("CONTRACT_NO/DATA/CONTRACT_NO");                 

         String ITEM_NO   = trans.getValue("ITEM_NO/DATA/ITEM_NO" );
         String UNIT_NO   = trans.getValue("ITEM0_ENTRY_QTY/DATA/ITEM0_ENTRY_QTY" );
         String MAT_NAME  = trans.getValue("ITEM0_UNIT_NO/DATA/ITEM0_UNIT_NO");
         String totlePrice = trans.getValue("ITEM0_TOTLE_PRICE/DATA/ITEM0_TOTLE_PRICE");
         trans.clear();
         cmd.clear();
         
         cmd = trans.addCustomFunction("ITEM_DESC","PROJECT_CONTRACT_ITEM_API.GET_ITEM_DESC","ITEM_DESC");
         cmd.addParameter("ITEM0_PROJ_NO");
         cmd.addParameter("CONTRACT_NO",contractId);
         cmd.addParameter("ITEM_NO",ITEM_NO);
         cmd = trans.addCustomFunction("ORIGINAL_QTY","PROJECT_CONTRACT_ITEM_API.GET_ORIGINAL_QTY","ORIGINAL_QTY");
         cmd.addParameter("ITEM0_PROJ_NO");
         cmd.addParameter("CONTRACT_NO",contractId);
         cmd.addParameter("ITEM_NO",ITEM_NO);
         trans = mgr.validate(trans);
         
         String ITEM_DESC   = trans.getValue("ITEM_DESC/DATA/ITEM_DESC" );
         String ORIGINAL_QTY   = trans.getValue("ORIGINAL_QTY/DATA/ORIGINAL_QTY" );
         
         ITEM_NO     = mgr.isEmpty(ITEM_NO    )? ""  : ITEM_NO    ;
         ITEM_DESC     = mgr.isEmpty(ITEM_DESC    )? ""  : ITEM_DESC    ;
         UNIT_NO     = mgr.isEmpty(UNIT_NO    )? ""  : UNIT_NO    ;
         MAT_NAME    = mgr.isEmpty(MAT_NAME    )? ""  : MAT_NAME    ;
         totlePrice    = mgr.isEmpty(totlePrice    )? ""  : totlePrice    ;
         ORIGINAL_QTY    = mgr.isEmpty(ORIGINAL_QTY    )? ""  : ORIGINAL_QTY    ;
         
         mgr.responseWrite(ITEM_NO + "^"+ ITEM_DESC + "^"+ ORIGINAL_QTY + "^"+ UNIT_NO + "^" +MAT_NAME + "^" + totlePrice + "^");
      }
      mgr.endResponse();
   }

