   public void makeReceipt(){
      ASPManager mgr = getASPManager();
      ASPTransactionBuffer trans = mgr.newASPTransactionBuffer();
      ASPCommand cmd;
      cmd = trans.addCustomCommand("PROJ_NO,RECEIPT_CODE","MAT_RECEIPT_API.Make_Receipt___");
      cmd.setParameter("PROJ_NO", headset.getValue("PROJ_NO"));
      cmd.setParameter("RECEIPT_CODE", headset.getValue("RECEIPT_CODE"));
      trans = mgr.perform(trans);
      cmd.clear();
      headset.refreshRow();
      okFindITEM1();
   }


   public void  adjust()
   {
      // fill function body
      if(headset.countRows() > 0&&headlay.isSingleLayout()&&!"已创建".equals(headset.getValue("RECEIPT_STATUS"))){
         headbar.disableCommand(headbar.EDITROW);
         headbar.disableCommand(headbar.DELETE);
         mat_receipt_line_bar.disableCommand(mat_receipt_line_bar.NEWROW);
         mat_receipt_line_bar.disableCommand(mat_receipt_line_bar.EDITROW);
         mat_receipt_line_bar.disableCommand(mat_receipt_line_bar.DELETE);
      }
   }

