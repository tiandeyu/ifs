   public void okFindITEM1()
   {
      ASPManager mgr = getASPManager();
      ASPTransactionBuffer trans = mgr.newASPTransactionBuffer();
      ASPQuery q;
      int headrowno;

      q = trans.addQuery(mat_receipt_line_blk);
      q.addWhereCondition("PROJ_NO = ? AND RECEIPT_CODE = ?");
      q.addParameter("PROJ_NO", headset.getValue("PROJ_NO"));
      q.addParameter("RECEIPT_CODE", headset.getValue("RECEIPT_CODE"));
      q.includeMeta("ALL");
      headrowno = headset.getCurrentRowNo();
      mgr.querySubmit(trans,mat_receipt_line_blk);
      headset.goTo(headrowno);
   }
