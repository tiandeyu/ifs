   public void makeBill(){
      ASPManager mgr = getASPManager();
      ASPTransactionBuffer trans = mgr.newASPTransactionBuffer();
      ASPCommand cmd;
      int headrowno;
      cmd = trans.addCustomCommand("PROJ_NO,BILL_CODE","MAT_BILL_API.Make_Bill___");
      cmd.setParameter("PROJ_NO", headset.getValue("PROJ_NO"));
      cmd.setParameter("BILL_CODE", headset.getValue("BILL_CODE"));
      trans = mgr.perform(trans);
      cmd.clear();
      headset.refreshRow();
      okFindITEM1();
   }

   public void  adjust()
   {
      // fill function body
      if(headset.countRows() > 0&&headlay.isSingleLayout()&&!"已创建".equals(headset.getValue("INVOICE_STATUS"))){
       headbar.disableCommand(headbar.EDITROW);
       headbar.disableCommand(headbar.DELETE);
       mat_bill_line_bar.disableCommand(mat_bill_line_bar.NEWROW);
       mat_bill_line_bar.disableCommand(mat_bill_line_bar.EDITROW);
       mat_bill_line_bar.disableCommand(mat_bill_line_bar.DELETE);
    }
   }

