   public void okFindITEM1()
   {
      ASPManager mgr = getASPManager();
      ASPTransactionBuffer trans = mgr.newASPTransactionBuffer();
      ASPQuery q;
      int headrowno;

      q = trans.addQuery(mat_sup_imp_list_blk);
      q.addWhereCondition("SUP_IMP_NO = ?");
      q.addParameter("SUP_IMP_NO", headset.getValue("SUP_IMP_NO"));
      q.includeMeta("ALL");
      headrowno = headset.getCurrentRowNo();
      mgr.querySubmit(trans,mat_sup_imp_list_blk);
      headset.goTo(headrowno);
   }
   public void newRowITEM1()
   {
      ASPManager mgr = getASPManager();
      ASPTransactionBuffer trans = mgr.newASPTransactionBuffer();
      ASPCommand cmd;
      ASPBuffer data;

      
      cmd = trans.addEmptyCommand("ITEM1","MAT_SUP_IMP_LIST_API.New__",mat_sup_imp_list_blk);
      cmd.setOption("ACTION","PREPARE");
      cmd.setParameter("ITEM0_SUP_IMP_NO", headset.getValue("SUP_IMP_NO"));
      trans = mgr.perform(trans);
      data = trans.getBuffer("ITEM1/DATA");
      mat_sup_imp_list_set.addRow(data);
   }
   
   
   public void validate()
   {
      ASPManager mgr=getASPManager();
      ASPTransactionBuffer trans=mgr.newASPTransactionBuffer();
      ASPCommand cmd;
      String str=mgr.readValue("VALIDATE");
    
      if ("SUP_PROJ_NO".equals(str)) {
         cmd = trans.addCustomFunction("SUP_PROJ_NAME","MAT_SUP_PROJECT_API.GET_SUP_PROJ_NAME","SUP_PROJ_NAME");
         cmd.addParameter("SUP_PROJ_NO");
         cmd = trans.addCustomFunction("UNIT_MODEL","MAT_SUP_PROJECT_API.GET_UNIT_MODEL","UNIT_MODEL");
         cmd.addParameter("SUP_PROJ_NO");
         cmd = trans.addCustomFunction("UNIT_SIZE","MAT_SUP_PROJECT_API.GET_UNIT_SIZE","UNIT_SIZE");
         cmd.addParameter("SUP_PROJ_NO");
         cmd = trans.addCustomFunction("UNIT_NUMBER","MAT_SUP_PROJECT_API.GET_UNIT_NUMBER","UNIT_NUMBER");
         cmd.addParameter("SUP_PROJ_NO");

         trans = mgr.validate(trans);
         String SUP_PROJ_NAME     = trans.getValue("SUP_PROJ_NAME/DATA/SUP_PROJ_NAME");
         String UNIT_MODEL     = trans.getValue("UNIT_MODEL/DATA/UNIT_MODEL");
         String UNIT_SIZE     = trans.getValue("UNIT_SIZE/DATA/UNIT_SIZE");
         String UNIT_NUMBER     = trans.getValue("UNIT_NUMBER/DATA/UNIT_NUMBER");
         
         mgr.responseWrite(SUP_PROJ_NAME + "^" + UNIT_MODEL + "^" + UNIT_SIZE + "^" + UNIT_NUMBER + "^" );
      }
      if ("LIST_NO".equals(str)) {
         cmd = trans.addCustomFunction("SUP_EQU_NO","MAT_SUP_PROJECT_LIST_API.GET_SUP_EQU_NO","SUP_EQU_NO");
         cmd.addParameter("SUP_PROJ_NO,LIST_NO");

         trans = mgr.validate(trans);
         String SUP_EQU_NO     = trans.getValue("SUP_EQU_NO/DATA/SUP_EQU_NO"            );

         SUP_EQU_NO     = mgr.isEmpty(SUP_EQU_NO    )? ""  : SUP_EQU_NO    ;
         
         mgr.responseWrite(SUP_EQU_NO + "^" );
      }
      
      mgr.endResponse();
   }

