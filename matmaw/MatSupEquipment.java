public class MatSupEquipment extends ASPPageProvider
{

   //-----------------------------------------------------------------------------
   //---------- Static constants ------------------------------------------------
   //-----------------------------------------------------------------------------

   public static boolean DEBUG = Util.isDebugEnabled("ifs.matmaw.MatSupEquipment");

   //-----------------------------------------------------------------------------
   //---------- Header Instances created on page creation --------
   //-----------------------------------------------------------------------------

   private ASPBlock headblk;
   private ASPRowSet headset;
   private ASPCommandBar headbar;
   private ASPTable headtbl;
   private ASPBlockLayout headlay;

   //-----------------------------------------------------------------------------
   //---------- Item Instances created on page creation --------
   //-----------------------------------------------------------------------------

   private ASPBlock mat_sup_equip_list_blk;
   private ASPRowSet mat_sup_equip_list_set;
   private ASPCommandBar mat_sup_equip_list_bar;
   private ASPTable mat_sup_equip_list_tbl;
   private ASPBlockLayout mat_sup_equip_list_lay;


   //-----------------------------------------------------------------------------
   //------------------------  Construction  ---------------------------
   //-----------------------------------------------------------------------------

   public  MatSupEquipment (ASPManager mgr, String page_path)
   {
      super(mgr,page_path);
   }

   public void run()
   {
      ASPManager mgr = getASPManager();

      if( mgr.commandBarActivated() )
         eval(mgr.commandBarFunction());
      else if(mgr.dataTransfered())
         okFind();
      else if( !mgr.isEmpty(mgr.getQueryStringValue("SEARCH")) )
         okFind();
      else if( !mgr.isEmpty(mgr.getQueryStringValue("SUP_EQU_NO")) )
         okFind();
      else 
         okFind();
      adjust();
   }
   //-----------------------------------------------------------------------------
   //------------------------  Command Bar functions  ---------------------------
   //-----------------------------------------------------------------------------

   public void okFind()
   {
      ASPManager mgr = getASPManager();
      ASPTransactionBuffer trans = mgr.newASPTransactionBuffer();
      ASPQuery q;

      mgr.createSearchURL(headblk);
      q = trans.addQuery(headblk);
      q.includeMeta("ALL");
      if(mgr.dataTransfered())
         q.addOrCondition(mgr.getTransferedData());
      mgr.querySubmit(trans,headblk);
      if (  headset.countRows() == 0 )
      {
         mgr.showAlert("MATSUPEQUIPMENTNODATA: No data found.");
         headset.clear();
      }
      eval( mat_sup_equip_list_set.syncItemSets() );
   }



   public void countFind()
   {
      ASPManager mgr = getASPManager();
      ASPTransactionBuffer trans = mgr.newASPTransactionBuffer();
      ASPQuery q;

      q = trans.addQuery(headblk);
      q.setSelectList("to_char(count(*)) N");
      mgr.submit(trans);
      headlay.setCountValue(toInt(headset.getValue("N")));
      headset.clear();
   }



   public void newRow()
   {
      ASPManager mgr = getASPManager();
      ASPTransactionBuffer trans = mgr.newASPTransactionBuffer();
      ASPBuffer data;
      ASPCommand cmd;

      cmd = trans.addEmptyCommand("HEAD","MAT_SUP_EQUIPMENT_API.New__",headblk);
      cmd.setOption("ACTION","PREPARE");
      trans = mgr.perform(trans);
      data = trans.getBuffer("HEAD/DATA");
      headset.addRow(data);
   }


   //-----------------------------------------------------------------------------
   //------------------------  Item block cmd bar functions  ---------------------------
   //-----------------------------------------------------------------------------


   public void okFindITEM1()
   {
      ASPManager mgr = getASPManager();
      ASPTransactionBuffer trans = mgr.newASPTransactionBuffer();
      ASPQuery q;
      int headrowno;

      q = trans.addQuery(mat_sup_equip_list_blk);
      q.addWhereCondition("SUP_EQU_NO = ?");
      q.addParameter("SUP_EQU_NO", headset.getValue("SUP_EQU_NO"));
      q.includeMeta("ALL");
      headrowno = headset.getCurrentRowNo();
      mgr.querySubmit(trans,mat_sup_equip_list_blk);
      headset.goTo(headrowno);
   }
   public void newRowITEM1()
   {
      ASPManager mgr = getASPManager();
      ASPTransactionBuffer trans = mgr.newASPTransactionBuffer();
      ASPCommand cmd;
      ASPBuffer data;

      
      cmd = trans.addEmptyCommand("ITEM1","MAT_SUP_EQUIP_LIST_API.New__",mat_sup_equip_list_blk);
      cmd.setOption("ACTION","PREPARE");
      cmd.setParameter("ITEM0_SUP_EQU_NO", headset.getValue("SUP_EQU_NO"));
      trans = mgr.perform(trans);
      data = trans.getBuffer("ITEM1/DATA");
      mat_sup_equip_list_set.addRow(data);
   }

   //-----------------------------------------------------------------------------
   //------------------------  Predefines Head ---------------------------
   //-----------------------------------------------------------------------------

   public void  preDefine()
   {
      ASPManager mgr = getASPManager();

      headblk = mgr.newASPBlock("MAIN");
      headblk.addField("OBJID").
              setHidden();
      headblk.addField("OBJVERSION").
              setHidden();
      headblk.addField("SUP_EQU_NO").
              setMandatory().
              setInsertable().
              setLabel("MATSUPEQUIPMENTSUPEQUNO: Sup Equ No").
              setSize(30);
      headblk.addField("PROJ_NO").
              setMandatory().
              setInsertable().
              setDynamicLOV("GENERAL_PROJECT").
              setLabel("MATSUPEQUIPMENTPROJNO: Proj No").
              setSize(30);
      headblk.addField("GENERAL_PROJECT_PROJ_DESC").
              setFunction("GENERAL_PROJECT_API.GET_PROJ_DESC ( :PROJ_NO)").
              setReadOnly().
              setLabel("MATSUPEQUIPMENTGENERALPROJECTPROJDESC: General Project Proj Desc").
              setSize(30);
               
      mgr.getASPField("PROJ_NO").setValidation("GENERAL_PROJECT_PROJ_DESC");
      headblk.addField("MAT_NO").
              setDynamicLOV("MAT_CODE","PROJ_NO").
              setInsertable().
              setLabel("MATSUPEQUIPMENTMATNO: Mat No").
              setSize(30);
      headblk.addField("MAT_NAME").
              setReadOnly().
              setFunction("MAT_CODE_API.GET_MAT_NAME ( :PROJ_NO, :MAT_NO)").
              setLabel("MATSUPEQUIPMENTMATNAME: Mat Name").
              setSize(30);
      mgr.getASPField("MAT_NO").setValidation("MAT_NAME");
      headblk.addField("PERSON_ID").
              setInsertable().
              setDynamicLOV("PERSON_INFO").
              setLabel("MATSUPEQUIPMENTPERSONID: Person Id").
              setSize(30);
      headblk.addField("PERSON_INFO_NAME").
              setFunction("PERSON_INFO_API.GET_NAME ( :PERSON_ID)").
              setReadOnly().
              setLabel("MATSUPEQUIPMENTPERSONINFONAME: Person Info Name").
              setSize(30);
      mgr.getASPField("PERSON_ID").setValidation("PERSON_INFO_NAME");

      headblk.addField("CREATE_TIME","Date").
              setInsertable().
              setLabel("MATSUPEQUIPMENTCREATETIME: Create Time").
              setSize(30);
//      headblk.addField("MAT_CODE_MAT_NAME").
//              setHidden().
//              setFunction("MAT_CODE_API.GET_MAT_NAME ( PROJ_NO,MAT_NO)").
//              setLabel("MATSUPEQUIPMENTMATCODEMATNAME: Mat Code Mat Name").
//              setSize(30);
      
      headblk.setView("MAT_SUP_EQUIPMENT");
      headblk.defineCommand("MAT_SUP_EQUIPMENT_API","New__,Modify__,Remove__");
      headset = headblk.getASPRowSet();
      headbar = mgr.newASPCommandBar(headblk);
      headtbl = mgr.newASPTable(headblk);
      headtbl.setTitle("MATSUPEQUIPMENTTBLHEAD: Mat Sup Equipments");
      headtbl.enableRowSelect();
      headtbl.setWrap();
      headlay = headblk.getASPBlockLayout();
      headlay.setDefaultLayoutMode(headlay.MULTIROW_LAYOUT);
      headlay.setSimple("GENERAL_PROJECT_PROJ_DESC");
      headlay.setSimple("MAT_NAME");
      headlay.setSimple("PERSON_INFO_NAME");
      headlay.setDataSpan("SUP_EQU_NO", 5);
 


      mat_sup_equip_list_blk = mgr.newASPBlock("ITEM1");
      mat_sup_equip_list_blk.addField("ITEM0_OBJID").
                             setHidden().
                             setDbName("OBJID");
      mat_sup_equip_list_blk.addField("ITEM0_OBJVERSION").
                             setHidden().
                             setDbName("OBJVERSION");
      mat_sup_equip_list_blk.addField("ITEM0_SUP_EQU_NO").
                             setDbName("SUP_EQU_NO").
                             setHidden().
                             setMandatory().
                             setInsertable().
                             setLabel("MATSUPEQUIPLISTITEM0SUPEQUNO: Sup Equ No").
                             setSize(50);
      mat_sup_equip_list_blk.addField("LIST_ID").
                             setMandatory().
                             setInsertable().
                             setLabel("MATSUPEQUIPLISTLISTID: List Id").
                             setSize(20);
      mat_sup_equip_list_blk.addField("SUP_TYPE").
                             enumerateValues("Sup_Type_API").
                             setSelectBox().
                             setInsertable().
                             setLabel("MATSUPEQUIPLISTSUPTYPE: Sup Type").
                             setSize(100);
      mat_sup_equip_list_blk.addField("EQUIP_NAME").
                             setInsertable().
                             setLabel("MATSUPEQUIPLISTEQUIPNAME: Equip Name").
                             setSize(20);
      mat_sup_equip_list_blk.addField("SUP_MSG").
                             setInsertable().
                             setLabel("MATSUPEQUIPLISTSUPMSG: Sup Msg").
                             setSize(30);
      mat_sup_equip_list_blk.addField("NOTE").
                             setInsertable().
                             setHeight(3).
                             setLabel("MATSUPEQUIPLISTNOTE: Note").
                             setSize(100);
     
      mat_sup_equip_list_blk.setView("MAT_SUP_EQUIP_LIST");
      mat_sup_equip_list_blk.defineCommand("MAT_SUP_EQUIP_LIST_API","New__,Modify__,Remove__");
      mat_sup_equip_list_blk.setMasterBlock(headblk);
      mat_sup_equip_list_set = mat_sup_equip_list_blk.getASPRowSet();
      mat_sup_equip_list_bar = mgr.newASPCommandBar(mat_sup_equip_list_blk);
      mat_sup_equip_list_bar.defineCommand(mat_sup_equip_list_bar.OKFIND, "okFindITEM1");
      mat_sup_equip_list_bar.defineCommand(mat_sup_equip_list_bar.NEWROW, "newRowITEM1");
      mat_sup_equip_list_tbl = mgr.newASPTable(mat_sup_equip_list_blk);
      mat_sup_equip_list_tbl.setTitle("MATSUPEQUIPLISTITEMHEAD1: MatSupEquipList");
      mat_sup_equip_list_tbl.enableRowSelect();
      mat_sup_equip_list_tbl.setWrap();
      mat_sup_equip_list_lay = mat_sup_equip_list_blk.getASPBlockLayout();
      mat_sup_equip_list_lay.setDefaultLayoutMode(mat_sup_equip_list_lay.MULTIROW_LAYOUT);
      mat_sup_equip_list_lay.setDataSpan("NOTE", 5);



   }



   public void  adjust()
   {
      // fill function body
   }

   //-----------------------------------------------------------------------------
   //------------------------  Presentation functions  ---------------------------
   //-----------------------------------------------------------------------------

   protected String getDescription()
   {
      return "MATSUPEQUIPMENTDESC: Mat Sup Equipment";
   }


   protected String getTitle()
   {
      return "MATSUPEQUIPMENTTITLE: Mat Sup Equipment";
   }


   protected void printContents() throws FndException
   {
      ASPManager mgr = getASPManager();
      if (headlay.isVisible())
          appendToHTML(headlay.show());
      else
      {
         headlay.setLayoutMode(headlay.CUSTOM_LAYOUT);
         appendToHTML(headlay.show());
      }
      if (mat_sup_equip_list_lay.isVisible())
          appendToHTML(mat_sup_equip_list_lay.show());

   }
}
