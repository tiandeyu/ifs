   public void submitCondition(){
      
      mgr = getASPManager();
      ASPCommand cmdBuf; 
      ASPTransactionBuffer trans = mgr.newASPTransactionBuffer();
      ctx   = mgr.getASPContext();
     
      trans.clear();
      String standard_info = "";
      headset.storeSelections();  
      if (headlay.isSingleLayout())
         headset.selectRow();
      ASPBuffer selected_fields=headset.getSelectedRows("NECE_NO");
      for(int i=0;i<selected_fields.countItems();i++){         
          ASPBuffer subBuff = selected_fields.getBufferAt(i);
          standard_info=standard_info + subBuff.getValueAt(0) + "^";
      }

      if((ctx.readValue("type")).equals("1")){        
         cmdBuf = trans.addCustomCommand("CREATECONDITION", "SELF_NECE_COND_LINE_API.Create_Nece_Condition");    
         cmdBuf.addParameter("OBJID", ctx.readValue("id"));
         cmdBuf.addParameter("PROJECT_TYPE_NO", ctx.readValue("proj_type_no"));
         cmdBuf.addParameter("NECE_NO", standard_info);
      }else if((ctx.readValue("type")).equals("2")){
         cmdBuf = trans.addCustomCommand("CREATECONDITION", "PRE_NECE_COND_LINE_API.Create_Nece_Condition");    
         cmdBuf.addParameter("OBJID", ctx.readValue("id"));
         cmdBuf.addParameter("PROJECT_TYPE_NO", ctx.readValue("proj_type_no"));
         cmdBuf.addParameter("NECE_NO", standard_info);
      }else if((ctx.readValue("type")).equals("3")){
         cmdBuf = trans.addCustomCommand("CREATECONDITION", "RE_NECE_COND_LINE_API.Create_Nece_Condition");    
         cmdBuf.addParameter("OBJID", ctx.readValue("id"));
         cmdBuf.addParameter("PROJECT_TYPE_NO", ctx.readValue("proj_type_no"));
         cmdBuf.addParameter("NECE_NO", standard_info);
      }
      mgr.perform(trans);
   }

