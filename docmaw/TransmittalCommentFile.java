public class TransmittalCommentFile extends ASPPageProvider
{

   //-----------------------------------------------------------------------------
   //---------- Static constants ------------------------------------------------
   //-----------------------------------------------------------------------------

   public static boolean DEBUG = Util.isDebugEnabled("ifs.docmaw.TransmittalCommentFile");

   //-----------------------------------------------------------------------------
   //---------- Header Instances created on page creation --------
   //-----------------------------------------------------------------------------

   private ASPBlock headblk;
   private ASPRowSet headset;
   private ASPCommandBar headbar;
   private ASPTable headtbl;
   private ASPBlockLayout headlay;
   private ASPBuffer       keys;

   private boolean bTranferToEDM;
   private boolean bCommentFileCheckedOut;
   private String sFilePath;

   //-----------------------------------------------------------------------------
   //------------------------  Construction  ---------------------------
   //-----------------------------------------------------------------------------

   public  TransmittalCommentFile (ASPManager mgr, String page_path)
   {
      super(mgr,page_path);
   }

   public void run()
   {
      ASPManager mgr = getASPManager();

      bCommentFileCheckedOut = false;


      if( mgr.commandBarActivated() )
         eval(mgr.commandBarFunction());
      else if(mgr.dataTransfered())
         okFind();
      else if( !mgr.isEmpty(mgr.getQueryStringValue("SEARCH")) )
         okFind();
      else if( !mgr.isEmpty(mgr.getQueryStringValue("DOC_CLASS")) )
         okFind();else if( !mgr.isEmpty(mgr.getQueryStringValue("DOC_NO")) )
         okFind();else if( !mgr.isEmpty(mgr.getQueryStringValue("DOC_SHEET")) )
         okFind();else if( !mgr.isEmpty(mgr.getQueryStringValue("DOC_REV")) )
         okFind();else if( !mgr.isEmpty(mgr.getQueryStringValue("DOC_TYPE")) )
         okFind();else if( !mgr.isEmpty(mgr.getQueryStringValue("FILE_NO")) )
         okFind();else if( !mgr.isEmpty(mgr.getQueryStringValue("TRANSMITTAL_ID")) )
         okFind();
      adjust();
   }
   //-----------------------------------------------------------------------------
   //------------------------  Command Bar functions  ---------------------------
   //-----------------------------------------------------------------------------

   public void okFind()
   {
      ASPManager mgr = getASPManager();
      ASPTransactionBuffer trans = mgr.newASPTransactionBuffer();
      ASPQuery q;

      mgr.createSearchURL(headblk);
      q = trans.addQuery(headblk);
      q.includeMeta("ALL");
      if(mgr.dataTransfered())
         q.addOrCondition(mgr.getTransferedData());
      mgr.querySubmit(trans,headblk);
      if (  headset.countRows() == 0 )
      {
         mgr.showAlert("DOCMAWTRANSMITTALCOMMENTFILENODATAFOUND: No data found.");
         headset.clear();
      }
   }



   public void countFind()
   {
      ASPManager mgr = getASPManager();
      ASPTransactionBuffer trans = mgr.newASPTransactionBuffer();
      ASPQuery q;

      q = trans.addQuery(headblk);
      q.setSelectList("to_char(count(*)) N");
      mgr.submit(trans);
      headlay.setCountValue(toInt(headset.getValue("N")));
      headset.clear();
   }



   public void newRow()
   {
      ASPManager mgr = getASPManager();
      ASPTransactionBuffer trans = mgr.newASPTransactionBuffer();
      ASPBuffer data;
      ASPCommand cmd;

      cmd = trans.addEmptyCommand("HEAD","TRANSMITTAL_COMMENT_FILE_API.New__",headblk);
      cmd.setOption("ACTION","PREPARE");
      trans = mgr.perform(trans);
      data = trans.getBuffer("HEAD/DATA");
      headset.addRow(data);
   }


   //-----------------------------------------------------------------------------
   //------------------------  Predefines Head ---------------------------
   //-----------------------------------------------------------------------------

   public void  preDefine()
   {
      ASPManager mgr = getASPManager();

      headblk = mgr.newASPBlock("MAIN");
      headblk.addField("OBJID").
              setHidden();
      headblk.addField("OBJVERSION").
              setHidden();
      //Bug Id 84461, Set the max length to 120
      headblk.addField("TRANSMITTAL_ID").
              setMandatory().
              setLabel("DOCMAWTRANSMITTALCOMMENTFILETRANSMITTALID: Transmittal Id").
              setSecureHyperlink("DocTransmittalInfo.page", "TRANSMITTAL_ID").
              setReadOnly().
              setMaxLength(120).
              setSize(10);
      headblk.addField("TRANSMITTAL_DESCRIPTION").
              setFunction("DOCUMENT_TRANSMITTAL_API.Get_Transmittal_Description(:TRANSMITTAL_ID)").
              setSize(20).
              setReadOnly().
              setMaxLength(800).
             setLabel("DOCMAWTRANSMITTALCOMMENTFILEDESCRIPTION: Description");

      headblk.addField("DOC_CLASS").
              setMandatory().
              setLabel("DOCMAWTRANSMITTALCOMMENTFILEDOCCLASS: Doc Class").
              setReadOnly().
              setSize(12);
      headblk.addField("DOC_NO").
              setMandatory().
              setLabel("DOCMAWTRANSMITTALCOMMENTFILEDOCNO: Doc No").
              setSecureHyperlink("DocIssue.page", "DOC_CLASS,DOC_NO").
              setReadOnly().
              setMaxLength(10).
              setSize(10);
      headblk.addField("DOC_SHEET").
              setMandatory().
              setLabel("DOCMAWTRANSMITTALCOMMENTFILEDOCSHEET: Doc Sheet").
              setReadOnly().
              setSize(10);
      headblk.addField("DOC_REV").
              setMandatory().
              setLabel("DOCMAWTRANSMITTALCOMMENTFILEDOCREV: Doc Rev").
              setReadOnly().
              setSize(6);
      headblk.addField("DOC_TYPE").
              setMandatory().
              setLabel("DOCMAWTRANSMITTALCOMMENTFILEDOCTYPE: Doc Type").
              setReadOnly().
              setSize(18);
      headblk.addField("FILE_NO","Number").
              setMandatory().
              setLabel("DOCMAWTRANSMITTALCOMMENTFILEFILENO: File No").
              setReadOnly().
              setSize(30);
      headblk.addField("FILE_NAME").
            setReadOnly().
            setFunction("EDM_FILE_API.Get_File_Name(DOC_CLASS,DOC_NO,DOC_SHEET,DOC_REV, DOC_TYPE,FILE_NO)").
            setLabel("DOCMAWTRANSMITTALCOMMENTFILEFILENAME: File Name");
 



      //EDM_FILE_API.Get_File_Name(doc_class,doc_no,doc_sheet,doc_rev,doc_type,FILE_NO)
     
      headblk.setView("TRANSMITTAL_COMMENT_FILE");
      //headblk.defineCommand("TRANSMITTAL_COMMENT_FILE_API","New__,Modify__,Remove__");
      headset = headblk.getASPRowSet();
      headbar = mgr.newASPCommandBar(headblk);
                     
      headbar.addCustomCommand("gotoDocInfo",mgr.translate("DOCMAWTRANSMITTALCOMMENTFILEDOCUMENTINFO: Document Info..."));
      headbar.addCustomCommand("gotoTransmittalInfo",mgr.translate("DOCMAWTRANSMITTALCOMMENTFILETRANSMITTALINFO: Transmittal Info..."));
      headbar.addSecureCustomCommand("viewCommentFromTransmittal",mgr.translate("DOCMAWTRANSMITTALCOMMENTFILEVIEWFILE: View File"),"DOCUMENT_ISSUE_ACCESS_API.Get_Document_Access"); //Bug Id 70286

      headbar.enableMultirowAction();
      headbar.removeFromMultirowAction("viewCommentFromTransmittal");
      
      headtbl = mgr.newASPTable(headblk);
      headtbl.setTitle("DOCMAWTRANSMITTALCOMMENTFILETBLHEAD: Document Transmittal Comment Files"); //Bug Id 68742
      headtbl.enableRowSelect();
      headtbl.setWrap();
      headlay = headblk.getASPBlockLayout();
      headlay.setDefaultLayoutMode(headlay.MULTIROW_LAYOUT);
 



   }



   public void  adjust()
   {
      // fill function body
   }

   //-----------------------------------------------------------------------------
   //------------------------  Presentation functions  ---------------------------
   //-----------------------------------------------------------------------------

   protected String getDescription()
   {
      //Bug Id 68742, start
      if (headlay.isMultirowLayout())
         return "DOCMAWTRANSMITTALOVWCOMMENTFILEDESC: Overview - Document Transmittal Comment Files";
      else
         return "DOCMAWTRANSMITTALCOMMENTFILEDESC: Document Transmittal Comment Files"; 
      //Bug Id 68742, end
      
   }


   protected String getTitle()
   {
      return getDescription(); //Bug Id 68742
   }


   public void  gotoTransmittalInfo()
   {
      ASPManager mgr = getASPManager();

      headset.store();
      mgr.transferDataTo("DocTransmittalInfo.page", headset.getSelectedRows("TRANSMITTAL_ID"));

   }

   public void  gotoDocInfo()
   {
      ASPManager mgr = getASPManager();

      headset.store();
      mgr.transferDataTo("DocIssue.page", headset.getSelectedRows("DOC_CLASS,DOC_NO,DOC_SHEET,DOC_REV"));

   }


   public void viewCommentFromTransmittal()
   {
      ASPManager mgr = getASPManager();

      if (headlay.isSingleLayout())
      {
         headset.unselectRows();
         headset.selectRow();
      }
      else
         headset.selectRows();

      ASPBuffer data = headset.getSelectedRows("DOC_CLASS,DOC_NO,DOC_SHEET,DOC_REV,FILE_NO");
      sFilePath = DocumentTransferHandler.getDataTransferUrl(mgr, "EdmMacro.page", "VIEWCOMMENT", "REDLINE", data);
      bTranferToEDM = true;
   }


   protected void printContents() throws FndException
   {
      ASPManager mgr = getASPManager();
      if (headlay.isVisible())
          appendToHTML(headlay.show());

      if (bTranferToEDM)
      {
         appendDirtyJavaScript("   var openWindow = true; \n");
         appendDirtyJavaScript("   if (");
         appendDirtyJavaScript(bCommentFileCheckedOut);
         appendDirtyJavaScript(" == true){ \n");
         appendDirtyJavaScript("    if (!confirm('");
         appendDirtyJavaScript(mgr.translate("DOCMAWTRANSMITTALCOMMENTFILECANNOTEDITDF: Comment file is checked out. Do you wish to continue editing the Original Document?"));
         appendDirtyJavaScript("'))\n");
         appendDirtyJavaScript("    openWindow = false; \n");
         appendDirtyJavaScript("   }\n");
         appendDirtyJavaScript("    if (openWindow)");
         appendDirtyJavaScript("   window.open(\"");
         appendDirtyJavaScript(sFilePath);
         appendDirtyJavaScript("\",\"anotherWindow\",\"status,resizable,scrollbars,width=500,height=500,left=100,top=100\");\n");
      }


   }
}
