//-----------------------------------------------------------------------------
//-----------------------------   Class def  ------------------------------
//-----------------------------------------------------------------------------

public class TransmittalLineState extends ASPPageProvider
{

   //-----------------------------------------------------------------------------
   //---------- Static constants ------------------------------------------------
   //-----------------------------------------------------------------------------

   public static boolean DEBUG = Util.isDebugEnabled("ifs.docmaw.TransmittalLineState");

   //-----------------------------------------------------------------------------
   //---------- Header Instances created on page creation --------
   //-----------------------------------------------------------------------------

   private ASPBlock headblk;
   private ASPRowSet headset;
   private ASPCommandBar headbar;
   private ASPTable headtbl;
   private ASPBlockLayout headlay;
   private ASPCommand cmd;

   //-----------------------------------------------------------------------------
   //------------------------  Construction  ---------------------------
   //-----------------------------------------------------------------------------

   public  TransmittalLineState (ASPManager mgr, String page_path)
   {
      super(mgr,page_path);
   }

   public void run()
   {
      ASPManager mgr = getASPManager();

      if( mgr.commandBarActivated() )
         eval(mgr.commandBarFunction());
      else if(mgr.dataTransfered())
         okFind();
      else if( !mgr.isEmpty(mgr.getQueryStringValue("SEARCH")) )
         okFind();
      else if( !mgr.isEmpty(mgr.getQueryStringValue("LINE_STATE")) )
         okFind();
      else if (!mgr.isEmpty(mgr.getQueryStringValue("VALIDATE")))
         validate();

        
      adjust();
   }
   //-----------------------------------------------------------------------------
   //------------------------  Command Bar functions  ---------------------------
   //-----------------------------------------------------------------------------

   public void okFind()
   {
      ASPManager mgr = getASPManager();
      ASPTransactionBuffer trans = mgr.newASPTransactionBuffer();
      ASPQuery q;

      mgr.createSearchURL(headblk);
      q = trans.addQuery(headblk);
      q.includeMeta("ALL");
      if(mgr.dataTransfered())
         q.addOrCondition(mgr.getTransferedData());
      mgr.querySubmit(trans,headblk);
      if (  headset.countRows() == 0 )
      {
         mgr.showAlert("TRANSMITTALLINESTATENODATA: No data found.");
         headset.clear();
      }
   }



   public void countFind()
   {
      ASPManager mgr = getASPManager();
      ASPTransactionBuffer trans = mgr.newASPTransactionBuffer();
      ASPQuery q;

      q = trans.addQuery(headblk);
      q.setSelectList("to_char(count(*)) N");
      mgr.submit(trans);
      headlay.setCountValue(toInt(headset.getValue("N")));
      headset.clear();
   }



   public void newRow()
   {
      ASPManager mgr = getASPManager();
      ASPTransactionBuffer trans = mgr.newASPTransactionBuffer();
      ASPBuffer data;
      ASPCommand cmd;

      cmd = trans.addEmptyCommand("HEAD","TRANSMITTAL_LINE_STATE_API.New__",headblk);
      cmd.setOption("ACTION","PREPARE");
      trans = mgr.perform(trans);
      data = trans.getBuffer("HEAD/DATA");
      headset.addRow(data);
   }


   //-----------------------------------------------------------------------------
   //------------------------  Predefines Head ---------------------------
   //-----------------------------------------------------------------------------

   public void  preDefine()
   {
      ASPManager mgr = getASPManager();

      headblk = mgr.newASPBlock("MAIN");
      headblk.addField("OBJID").
              setHidden();
      headblk.addField("OBJVERSION").
              setHidden();
      headblk.addField("PROJECT_ID").
              setMandatory().
              setInsertable().
              setReadOnly().
              setDynamicLOV("PROJECT").
              setCustomValidation("PROJECT_ID","PROJECT_NAME, PROJECT_DESCRIPTION").
              setLabel("TRANSMITTALLINESTATEPROJECTID: Project Id").
              setSize(10);
      headblk.addField("PROJECT_NAME").
              setFunction("PROJECT_API.GET_NAME(:PROJECT_ID)").
              setReadOnly().
              setLabel("TRANSMITTALLINESTATEPROJECTNAME: Project Name").
              setSize(30);
      headblk.addField("PROJECT_DESCRIPTION").
              setFunction("PROJECT_API.GET_DESCRIPTION (:PROJECT_ID)").
              setReadOnly().
              setLabel("TRANSMITTALLINESTATEPROJECTDESCRIPTION: Project Description").
              setSize(30);
      headblk.addField("LINE_STATE").
              setMandatory().
              setInsertable().
              setReadOnly().
              setLabel("TRANSMITTALLINESTATELINESTATE: Line State").
              setSize(20);

              
      headblk.setView("TRANSMITTAL_LINE_STATE");
      headblk.defineCommand("TRANSMITTAL_LINE_STATE_API","New__,Remove__");
      headset = headblk.getASPRowSet();
      
      headbar = mgr.newASPCommandBar(headblk);
      headbar.disableCommand(headbar.EDITROW);

      headtbl = mgr.newASPTable(headblk);
      headtbl.setTitle("TRANSMITTALLINESTATETBLHEAD: Statuses for Document Transmittal Lines"); //Bug Id 68742
      headtbl.enableRowSelect();
      headtbl.setWrap();
      headlay = headblk.getASPBlockLayout();
      headlay.setDefaultLayoutMode(headlay.MULTIROW_LAYOUT);



   }

   public void validate()
   {
      
      ASPManager mgr = getASPManager();
      ASPTransactionBuffer trans = mgr.newASPTransactionBuffer();
      
      
      String val = mgr.readValue("VALIDATE");
      
      if (val.equals("PROJECT_ID")) {
         trans.clear();
         cmd = trans.addCustomFunction("PROJNAME", "PROJECT_API.GET_NAME", "PROJECT_NAME");
         cmd.addParameter("PROJECT_ID");

         cmd = trans.addCustomFunction("PROJDESC", "PROJECT_API.GET_DESCRIPTION", "PROJECT_DESCRIPTION");
         cmd.addParameter("PROJECT_ID");

         trans = mgr.validate(trans);
      
         String projName         = trans.getValue("PROJNAME/DATA/PROJECT_NAME");
         String projDesc         = trans.getValue("PROJDESC/DATA/PROJECT_DESCRIPTION");
         StringBuffer response = new StringBuffer("");
         response.append(projName);
         response.append("^");
         response.append(projDesc);
         response.append("^");

         mgr.responseWrite(response.toString());
      }
      mgr.endResponse();
      

   }


   public void  adjust()
   {
      // fill function body
   }

   //-----------------------------------------------------------------------------
   //------------------------  Presentation functions  ---------------------------
   //-----------------------------------------------------------------------------

   protected String getDescription()
   {
      return "TRANSMITTALLINESTATEDESC: Statuses for Document Transmittal Lines"; //Bug Id 68742
   }


   protected String getTitle()
   {
      return "TRANSMITTALLINESTATETITLE: Statuses for Document Transmittal Lines"; //Bug Id 68742
   }


   protected void printContents() throws FndException
   {
      ASPManager mgr = getASPManager();
      if (headlay.isVisible()){
         appendToHTML(headlay.show());
      }

   }
}
