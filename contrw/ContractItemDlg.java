   public void run()
   {
      ASPManager mgr = getASPManager();

      if( mgr.commandBarActivated() )
         eval(mgr.commandBarFunction());
      else if(mgr.dataTransfered())
         okFind();
      else if( !mgr.isEmpty(mgr.getQueryStringValue("SEARCH")) )
         okFind();
      else if (mgr.buttonPressed("SUBMIT")) {
         submitItem();  
      }    
      else if( !mgr.isEmpty(mgr.getQueryStringValue("ITEM_NO")) )
         okFind();
      else 
         okFind();  
      adjust();
   }
   
   public void submitItem(){
      ASPManager mgr = getASPManager();
       ASPCommand cmdBuf;       
       ASPTransactionBuffer trans = mgr.newASPTransactionBuffer();
      
       trans.clear();

       headset.storeSelections();  
       if (headlay.isSingleLayout())  
          headset.selectRow();
       ASPBuffer selected_fields=headset.getSelectedRows("PROJ_NO,CONTRACT_ID,REPORT_NO,ITEM_NO");
       for(int i=0;i<selected_fields.countItems();i++){         
           ASPBuffer subBuff = selected_fields.getBufferAt(i);
           
           cmdBuf = trans.addCustomCommand("CREATETREE"+i, "CONTRACT_REPORT_QTY_ITEM_API.Modify_Contract_Report_Item");
           cmdBuf.addParameter("PROJ_NO", subBuff.getValueAt(0)); 
           cmdBuf.addParameter("CONTRACT_ID", subBuff.getValueAt(1));     
           cmdBuf.addParameter("REPORT_NO", subBuff.getValueAt(2));
           cmdBuf.addParameter("ITEM_NO", subBuff.getValueAt(3));
           trans=mgr.perform(trans);     
           trans.clear();      
       }
       
    }
