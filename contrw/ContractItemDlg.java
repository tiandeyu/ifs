   public void okFind()
   {
      ASPManager mgr = getASPManager();
      ASPTransactionBuffer trans = mgr.newASPTransactionBuffer();
      ASPQuery q;
      ASPContext ctx = mgr.getASPContext();  
      mgr.createSearchURL(headblk);
      q = trans.addQuery(headblk);
      q.addWhereCondition("PROJ_NO = ? AND CONTRACT_ID = ? AND REPORT_NO = ? AND ITEM_TYPE = ? AND (IS_CURR_REPORT IS NULL OR IS_CURR_REPORT = 'FALSE')");
      q.addParameter("PROJ_NO",ctx.findGlobal("CONTRACTITEMDLGPROJNO"));
      q.addParameter("CONTRACT_ID",ctx.findGlobal("CONTRACTITEMDLGCONTRACTID"));
      q.addParameter("REPORT_NO",ctx.findGlobal("CONTRACTITEMDLGREPORTNO"));
      q.addParameter("ITEM_TYPE","ITEM");              
      q.includeMeta("ALL");
      if(mgr.dataTransfered())
         q.addOrCondition(mgr.getTransferedData());
      mgr.querySubmit(trans,headblk);
      if (  headset.countRows() == 0 )
      {
         mgr.showAlert("CONTRACTREPORTQTYITEMNODATA: No data found.");
         headset.clear();
      }
   }



   public void countFind()
   {
      ASPManager mgr = getASPManager();
      ASPTransactionBuffer trans = mgr.newASPTransactionBuffer();
      ASPQuery q;

      q = trans.addQuery(headblk);
      q.setSelectList("to_char(count(*)) N");
      mgr.submit(trans);
      headlay.setCountValue(toInt(headset.getValue("N")));
      headset.clear();
   }



   public void newRow()
   {
      ASPManager mgr = getASPManager();
      ASPTransactionBuffer trans = mgr.newASPTransactionBuffer();
      ASPBuffer data;
      ASPCommand cmd;

      cmd = trans.addEmptyCommand("HEAD","CONTRACT_REPORT_QTY_ITEM_API.New__",headblk);
      cmd.setOption("ACTION","PREPARE");
      trans = mgr.perform(trans);
      data = trans.getBuffer("HEAD/DATA");
      headset.addRow(data);
   }


