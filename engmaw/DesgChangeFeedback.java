   public void validate()
   {
      ASPManager mgr=getASPManager();
      ASPTransactionBuffer trans0=mgr.newASPTransactionBuffer();
      ASPCommand cmd0;
      String str=mgr.readValue("VALIDATE");
      if ("NOTICE_NO".equals(str)) {
         
         cmd0 = trans0.addCustomFunction("NOTICE_NAME","DESG_CHANGE_NOTICE_API.Get_NOTICE_NAME","NOTICE_NAME");
         cmd0.addParameter("PROJ_NO");
         cmd0.addParameter("NOTICE_NO");
         cmd0 = trans0.addCustomFunction("CONTRACT_ID","DESG_CHANGE_NOTICE_API.GET_CONTRACT_ID","CONTRACT_ID");
         cmd0.addParameter("PROJ_NO");
         cmd0.addParameter("NOTICE_NO");
         cmd0 = trans0.addCustomFunction("VOLUME_NO","DESG_CHANGE_NOTICE_API.GET_VOLUME_NO","VOLUME_NO");
         cmd0.addParameter("PROJ_NO");
         cmd0.addParameter("NOTICE_NO");
         cmd0 = trans0.addCustomFunction("TOTLE_LIST_PRICE","DESG_CHANGE_NOTICE_API.GET_TOTLE_LIST_PRICE","TOTLE_LIST_PRICE");
         cmd0.addParameter("PROJ_NO");
         cmd0.addParameter("NOTICE_NO");
         cmd0 = trans0.addCustomFunction("SEND_TO","DESG_CHANGE_NOTICE_API.GET_SEND_TO","SEND_TO");
         cmd0.addParameter("PROJ_NO");
         cmd0.addParameter("NOTICE_NO");
         cmd0 = trans0.addCustomFunction("COPY_TO","DESG_CHANGE_NOTICE_API.GET_COPY_TO","COPY_TO");
         cmd0.addParameter("PROJ_NO");
         cmd0.addParameter("NOTICE_NO");

         trans0 = mgr.validate(trans0);

         String NOTICE_NAME     = trans0.getValue("NOTICE_NAME/DATA/NOTICE_NAME");
         String CONTRACT_ID     = trans0.getValue("CONTRACT_ID/DATA/CONTRACT_ID");
         String VOLUME_NO     = trans0.getValue("VOLUME_NO/DATA/VOLUME_NO");
         String TOTLE_LIST_PRICE     = trans0.getValue("TOTLE_LIST_PRICE/DATA/TOTLE_LIST_PRICE");
         String SEND_TO     = trans0.getValue("SEND_TO/DATA/SEND_TO");
         String COPY_TO     = trans0.getValue("COPY_TO/DATA/COPY_TO");

         NOTICE_NAME     = mgr.isEmpty(NOTICE_NAME    )? ""  : NOTICE_NAME    ;
         CONTRACT_ID     = mgr.isEmpty(CONTRACT_ID    )? ""  : CONTRACT_ID    ;
         VOLUME_NO     = mgr.isEmpty(VOLUME_NO    )? ""  : VOLUME_NO    ;
         TOTLE_LIST_PRICE     = mgr.isEmpty(TOTLE_LIST_PRICE    )? ""  : TOTLE_LIST_PRICE    ;
         SEND_TO     = mgr.isEmpty(SEND_TO    )? ""  : SEND_TO    ;
         COPY_TO     = mgr.isEmpty(COPY_TO    )? ""  : COPY_TO    ;
         trans0.clear();
         cmd0 = trans0.addCustomFunction("CONTRACT_DESC","PROJECT_CONTRACT_API.GET_CONTRACT_DESC","CONTRACT_DESC");
         cmd0.addParameter("PROJ_NO");
         cmd0.addParameter("CONTRACT_ID",CONTRACT_ID);
         cmd0 = trans0.addCustomFunction("VOLUME_DESC","DRAWING_LIST_API.GET_VOLUME_DESC","VOLUME_DESC");
         cmd0.addParameter("PROJ_NO");
         cmd0.addParameter("VOLUME_NO",VOLUME_NO);
         cmd0 = trans0.addCustomFunction("SEND_TO_NAME","GENERAL_ORGANIZATION_API.GET_ORG_DESC","SEND_TO_NAME");
         cmd0.addParameter("SEND_TO",SEND_TO);
         cmd0 = trans0.addCustomFunction("COPY_TO_NAME","GENERAL_ORGANIZATION_API.GET_ORG_DESC","COPY_TO_NAME");
         cmd0.addParameter("COPY_TO",COPY_TO);

         trans0 = mgr.validate(trans0);

         String CONTRACT_DESC     = trans0.getValue("CONTRACT_DESC/DATA/CONTRACT_DESC");
         String VOLUME_DESC     = trans0.getValue("VOLUME_DESC/DATA/VOLUME_DESC");
         String SEND_TO_NAME     = trans0.getValue("SEND_TO_NAME/DATA/SEND_TO_NAME");
         String COPY_TO_NAME     = trans0.getValue("COPY_TO_NAME/DATA/COPY_TO_NAME");

         CONTRACT_DESC     = mgr.isEmpty(CONTRACT_DESC    )? ""  : CONTRACT_DESC    ;
         VOLUME_DESC     = mgr.isEmpty(VOLUME_DESC    )? ""  : VOLUME_DESC    ;
         SEND_TO_NAME     = mgr.isEmpty(SEND_TO_NAME    )? ""  : SEND_TO_NAME    ;
         COPY_TO_NAME     = mgr.isEmpty(COPY_TO_NAME    )? ""  : COPY_TO_NAME    ;
         mgr.responseWrite(NOTICE_NAME + "^"+ CONTRACT_ID + "^"+ CONTRACT_DESC + "^"+ VOLUME_NO + "^"+ VOLUME_DESC + "^"+ SEND_TO + "^"+ COPY_TO + "^"+ SEND_TO_NAME + "^"+ COPY_TO_NAME + "^"+ TOTLE_LIST_PRICE + "^");
      }
      else if ("QUANTITIES".equals(str)||"PRICE".equals(str)) {
         mgr.responseWrite((mgr.isEmpty(mgr.readValue("QUANTITIES"))?0:Float.parseFloat(mgr.readValue("QUANTITIES")))*(mgr.isEmpty(mgr.readValue("PRICE"))?0:Float.parseFloat(mgr.readValue("PRICE")))+ "^");
      }
      
      mgr.endResponse();
   }
   
   
   
