   public void  adjust()
   {
      // fill function body
      if(headlay.isSingleLayout()&&headset.countRows()>0){
         String state = headset.getValue("OBJSTATE");
         if( headlay.isSingleLayout() && ("Closed".equals(state) || "Checked".equals(state))){ 
               headbar.disableCommand(headbar.DELETE);
               headbar.disableCommand(headbar.EDITROW);   
               drawing_back_line_bar.disableCommand( drawing_back_line_bar.DELETE );
               drawing_back_line_bar.disableCommand( drawing_back_line_bar.EDITROW );  
         }    
     }
   }

