   public void run()
   {
      ASPManager mgr = getASPManager();

      if( mgr.commandBarActivated() )
         eval(mgr.commandBarFunction());
      else if(mgr.dataTransfered())
         okFind();
      else if( !mgr.isEmpty(mgr.getQueryStringValue("SEARCH")) )
         okFind();
      else if (mgr.buttonPressed("SUBMIT")) {
         submitItem();  
      }    
      else if( !mgr.isEmpty(mgr.getQueryStringValue("VOLUME_NO")) )
         okFind();
      else
         okFind();
      adjust();
   }
   
   
   public void submitItem(){
      ASPManager mgr = getASPManager();
      ASPContext ctx =mgr.getASPContext();
       ASPCommand cmdBuf;       
       ASPTransactionBuffer trans = mgr.newASPTransactionBuffer();
       trans.clear();
       headset.storeSelections();  
       if (headlay.isSingleLayout())  
          headset.selectRow();
       ASPBuffer selected_fields=headset.getSelectedRows("VOLUME_NO");
       for(int i=0;i<selected_fields.countItems();i++){         
           ASPBuffer subBuff = selected_fields.getBufferAt(i);
           cmdBuf = trans.addCustomCommand("CREATETREE"+i, "DRAWING_SEND_LINE_API.Create_Drawing_Send_Line");
           cmdBuf.addParameter("PROJ_NO", ctx.findGlobal("DRAWINGLISTDLGPROJNO")); 
           cmdBuf.addParameter("ID", ctx.findGlobal("DRAWINGLISTDLGID"));     
           cmdBuf.addParameter("VOLUME_NO", subBuff.getValueAt(0)); 
//           trans=mgr.perform(trans);      
       }          
       mgr.perform(trans);
 
    }   
   
