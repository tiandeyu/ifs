   public void okFind()
   {
      ASPManager mgr = getASPManager();
      ASPContext ctx =mgr.getASPContext();  
      ASPTransactionBuffer trans = mgr.newASPTransactionBuffer();
      ASPQuery q;

      mgr.createSearchURL(headblk);
      q = trans.addQuery(headblk);
      q.addWhereCondition("PROJ_NO = '"+ctx.findGlobal("DRAWINGLISTDLGPROJNO")+"'");
      q.addWhereCondition("VOLUME_NO NOT IN (SELECT VOLUME_NO FROM IFSAPP.DRAWING_SEND_LINE WHERE ID = '"+ctx.findGlobal("DRAWINGLISTDLGID")+"' )");
      q.addWhereCondition("STATUS_NO != 'YFF'");
      q.includeMeta("ALL");    
      if(mgr.dataTransfered())        
         q.addOrCondition(mgr.getTransferedData());
      mgr.querySubmit(trans,headblk);    
      if (  headset.countRows() == 0 )
      {
         mgr.showAlert("DRAWINGLISTNODATA: No data found.");
         headset.clear();
      }
   }



   public void countFind()
   {
      ASPManager mgr = getASPManager();
      ASPTransactionBuffer trans = mgr.newASPTransactionBuffer();
      ASPQuery q;

      q = trans.addQuery(headblk);
      q.setSelectList("to_char(count(*)) N");
      mgr.submit(trans);
      headlay.setCountValue(toInt(headset.getValue("N")));
      headset.clear();
   }



   public void newRow()
   {
      ASPManager mgr = getASPManager();
      ASPTransactionBuffer trans = mgr.newASPTransactionBuffer();
      ASPBuffer data;
      ASPCommand cmd;

      cmd = trans.addEmptyCommand("HEAD","DRAWING_LIST_API.New__",headblk);
      cmd.setOption("ACTION","PREPARE");
      cmd.setParameter("PROJ_NO", this.getDefProj());  
      trans = mgr.perform(trans);
      data = trans.getBuffer("HEAD/DATA");
      headset.addRow(data);
   }


