  public void submitCondition(){
      
      mgr = getASPManager();
      ASPCommand cmdBuf; 
      ASPTransactionBuffer trans = mgr.newASPTransactionBuffer();
      ctx   = mgr.getASPContext();
     
      trans.clear();
      String standard_info = "";
      headset.storeSelections();  
      if (headlay.isSingleLayout())
         headset.selectRow();
      ASPBuffer selected_fields=headset.getSelectedRows("BUDGET_LINE_NO");
      for(int i=0;i<selected_fields.countItems();i++){         
          ASPBuffer subBuff = selected_fields.getBufferAt(i);
          standard_info=standard_info + subBuff.getValueAt(0) + "^";
      }

      cmdBuf = trans.addCustomCommand("CREATEBUDGETLINENO", "BID_PROJ_LIST_API.Create_Budget_No");    
      cmdBuf.addParameter("PROJ_NO", ctx.readValue("proj_no"));
      cmdBuf.addParameter("PROJECT_TYPE_ID", ctx.readValue("plan_no"));
      cmdBuf.addParameter("REPORT_TYPE_ID", ctx.readValue("doc_id"));
      cmdBuf.addParameter("BUDGET_LINE_NO", standard_info);
      mgr.perform(trans);
   }


