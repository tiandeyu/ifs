   public  BidMatPurchLineDlg (ASPManager mgr, String page_path)
   {
      super(mgr,page_path);
   }

   public void run()
   {
      ASPManager mgr = getASPManager();
      if (mgr.dataTransfered())
         storeParamters();
      if( mgr.commandBarActivated() )
         eval(mgr.commandBarFunction());
      else if(mgr.dataTransfered())
         okFind();
      else if (mgr.buttonPressed("SUBMIT")) 
         submitCondition();
      else 
         okFind();
      adjust();
   }
   
   public void storeParamters() 
   {
      ASPManager mgr = getASPManager();

      if (mgr.dataTransfered())
      {
         ASPContext ctx = mgr.getASPContext();
         ASPBuffer buf = mgr.getTransferedData();
         String proj_no = buf.getValue("DATA/PROJ_NO");
         String purch_id = buf.getValue("DATA/PURCH_ID");
         if (!mgr.isEmpty(proj_no))
            ctx.setGlobal("PROJ_NO", proj_no);
         if (!mgr.isEmpty(purch_id))
            ctx.setGlobal("PURCH_ID", purch_id);
      }
   }
   
   //-----------------------------------------------------------------------------
   //------------------------  Command Bar functions  ---------------------------
   //-----------------------------------------------------------------------------

   public void okFind()
   {
      ASPManager mgr = getASPManager();
      ASPTransactionBuffer trans = mgr.newASPTransactionBuffer();
      ASPQuery q;
      ASPContext ctx = mgr.getASPContext();

      mgr.createSearchURL(headblk);
      q = trans.addQuery(headblk);
      q.addWhereCondition(" PROJ_NO = '"+ctx.findGlobal("PROJ_NO")+"' AND IS_PURCHED = 'FALSE' ");
      q.addWhereCondition("BID_MAT_REQ_API.GET_STATUS( PROJ_NO, REQ_ID)='2'");
      q.includeMeta("ALL");
//      if(mgr.dataTransfered())
//         q.addOrCondition(mgr.getTransferedData());
      mgr.querySubmit(trans,headblk);
      if (  headset.countRows() == 0 )
      {
         mgr.showAlert("BIDMATPURCHLINEDLGNODATA: No data found.");
         headset.clear();
      }
   }



   public void countFind()
   {
      ASPManager mgr = getASPManager();
      ASPTransactionBuffer trans = mgr.newASPTransactionBuffer();
      ASPQuery q;

      q = trans.addQuery(headblk);
      q.setSelectList("to_char(count(*)) N");
      mgr.submit(trans);
      headlay.setCountValue(toInt(headset.getValue("N")));
      headset.clear();
   }



   public void newRow()
   {
      ASPManager mgr = getASPManager();
      ASPTransactionBuffer trans = mgr.newASPTransactionBuffer();
      ASPBuffer data;
      ASPCommand cmd;

      cmd = trans.addEmptyCommand("HEAD","BID_MAT_REQ_LINE_API.New__",headblk);
      cmd.setOption("ACTION","PREPARE");
      trans = mgr.perform(trans);
      data = trans.getBuffer("HEAD/DATA");
      headset.addRow(data);
   }


   //-----------------------------------------------------------------------------
   //------------------------  Predefines Head ---------------------------
