      headblk.addField("BUDGET_TOTAL_PRICE","Number").setLabel("BIDMATREQLINEBUDGETTOTALPRICE: Budget Total Price").setSize(20);
      headblk.addField("USED_PART").setLabel("BIDMATREQLINEUSEDPART: Used Part").setSize(20);
