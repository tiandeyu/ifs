package ifs.conmaw;
//-----------------------------------------------------------------------------
//-----------------------------   Import def  ------------------------------
//-----------------------------------------------------------------------------

import ifs.fnd.asp.*;
import ifs.fnd.buffer.*;
import ifs.fnd.service.*;
import ifs.fnd.*;

//-----------------------------------------------------------------------------
//-----------------------------   Class def  ------------------------------
//-----------------------------------------------------------------------------

public class ConDocTypeData extends ASPPageProvider
{

   //-----------------------------------------------------------------------------
   //---------- Static constants ------------------------------------------------
   //-----------------------------------------------------------------------------

   public static boolean DEBUG = Util.isDebugEnabled("ifs.conmaw.ConDocTypeData");

   //-----------------------------------------------------------------------------
   //---------- Header Instances created on page creation --------
   //-----------------------------------------------------------------------------

   private ASPBlock headblk;
   private ASPRowSet headset;
   private ASPCommandBar headbar;
   private ASPTable headtbl;
   private ASPBlockLayout headlay;

   //-----------------------------------------------------------------------------
   //---------- Item Instances created on page creation --------
   //-----------------------------------------------------------------------------

   private ASPBlock con_doc_type_data_blk;
   private ASPRowSet con_doc_type_data_set;
   private ASPCommandBar con_doc_type_data_bar;
   private ASPTable con_doc_type_data_tbl;
   private ASPBlockLayout con_doc_type_data_lay;


   //-----------------------------------------------------------------------------
   //------------------------  Construction  ---------------------------
   //-----------------------------------------------------------------------------

   public  ConDocTypeData (ASPManager mgr, String page_path)
   {
      super(mgr,page_path);
   }

   public void run()
   {
      ASPManager mgr = getASPManager();

      if( mgr.commandBarActivated() )
         eval(mgr.commandBarFunction());
      else if(mgr.dataTransfered())
         okFind();
      else if( !mgr.isEmpty(mgr.getQueryStringValue("SEARCH")) )
         okFind();
      else if( !mgr.isEmpty(mgr.getQueryStringValue("PROJ_NO")) )
         okFind();
      else
         okFind();
      adjust();
   }
   //-----------------------------------------------------------------------------
   //------------------------  Command Bar functions  ---------------------------
   //-----------------------------------------------------------------------------

   public void okFind()
   {
      ASPManager mgr = getASPManager();
      ASPTransactionBuffer trans = mgr.newASPTransactionBuffer();
      ASPQuery q;

      mgr.createSearchURL(headblk);
      q = trans.addQuery(headblk);
      q.includeMeta("ALL");
      if(mgr.dataTransfered())
         q.addOrCondition(mgr.getTransferedData());
      mgr.querySubmit(trans,headblk);
      if (  headset.countRows() == 0 )
      {
         mgr.showAlert("CONDOCTYPEDATANODATA: No data found.");
         headset.clear();
      }
      eval( con_doc_type_data_set.syncItemSets() );
   }



   public void countFind()
   {
      ASPManager mgr = getASPManager();
      ASPTransactionBuffer trans = mgr.newASPTransactionBuffer();
      ASPQuery q;

      q = trans.addQuery(headblk);
      q.setSelectList("to_char(count(*)) N");
      mgr.submit(trans);
      headlay.setCountValue(toInt(headset.getValue("N")));
      headset.clear();
   }



   public void newRow()
   {
      ASPManager mgr = getASPManager();
      ASPTransactionBuffer trans = mgr.newASPTransactionBuffer();
      ASPBuffer data;
      ASPCommand cmd;

      cmd = trans.addEmptyCommand("HEAD","GENERAL_PROJECT_API.New__",headblk);
      cmd.setOption("ACTION","PREPARE");
      trans = mgr.perform(trans);
      data = trans.getBuffer("HEAD/DATA");
      headset.addRow(data);
   }


   //-----------------------------------------------------------------------------
   //------------------------  Item block cmd bar functions  ---------------------------
   //-----------------------------------------------------------------------------


   public void okFindITEM1()
   {
      ASPManager mgr = getASPManager();
      ASPTransactionBuffer trans = mgr.newASPTransactionBuffer();
      ASPQuery q;
      int headrowno;

      q = trans.addQuery(con_doc_type_data_blk);
      q.addWhereCondition("PROJ_NO = ?");
      q.addParameter("PROJ_NO", headset.getValue("PROJ_NO"));
      q.includeMeta("ALL");
      headrowno = headset.getCurrentRowNo();
      mgr.querySubmit(trans,con_doc_type_data_blk);
      headset.goTo(headrowno);
   }
   public void newRowITEM1()
   {
      ASPManager mgr = getASPManager();
      ASPTransactionBuffer trans = mgr.newASPTransactionBuffer();
      ASPCommand cmd;
      ASPBuffer data;

      
      cmd = trans.addEmptyCommand("ITEM1","CON_DOC_TYPE_DATA_API.NEW__",con_doc_type_data_blk);
      cmd.setOption("ACTION","PREPARE");
      cmd.setParameter("ITEM0_PROJ_NO", headset.getValue("PROJ_NO"));
      trans = mgr.perform(trans);
      data = trans.getBuffer("ITEM1/DATA");
      con_doc_type_data_set.addRow(data);
   }

   //-----------------------------------------------------------------------------
   //------------------------  Predefines Head ---------------------------
   //-----------------------------------------------------------------------------

   public void  preDefine()
   {
      ASPManager mgr = getASPManager();

      headblk = mgr.newASPBlock("MAIN");
      headblk.addField("OBJID").
              setHidden();
      headblk.addField("OBJVERSION").
              setHidden();
      headblk.addField("PROJ_NO").
              setMandatory().
              setInsertable().
              setLabel("CONDOCTYPEDATAPROJNO: Proj No").
              setSize(50);
      headblk.addField("PROJ_DESC").
              setInsertable().
              setLabel("CONDOCTYPEDATAPROJDESC: Proj Desc").
              setSize(200);
      headblk.addField("NOTE").
              setInsertable().
              setLabel("CONDOCTYPEDATANOTE: Note").
              setSize(2000);
      headblk.setView("GENERAL_PROJECT");
      headblk.defineCommand("GENERAL_PROJECT_API",",,");
      headset = headblk.getASPRowSet();
      headbar = mgr.newASPCommandBar(headblk);
      headtbl = mgr.newASPTable(headblk);
      headtbl.setTitle("CONDOCTYPEDATATBLHEAD: Con Doc Type Data");
      headtbl.enableRowSelect();
      headtbl.setWrap();
      headlay = headblk.getASPBlockLayout();
      headlay.setDefaultLayoutMode(headlay.MULTIROW_LAYOUT);
 


      con_doc_type_data_blk = mgr.newASPBlock("ITEM1");
      con_doc_type_data_blk.addField("ITEM0_OBJID").
                      setHidden().
                      setDbName("OBJID");
      con_doc_type_data_blk.addField("ITEM0_OBJVERSION").
                      setHidden().
                      setDbName("OBJVERSION");
      con_doc_type_data_blk.addField("ITEM0_PROJ_NO").
                      setDbName("PROJ_NO").
                      setMandatory().
                      setHidden().
                      setLabel("CONDOCTYPEDATAITEM0PROJNO: Proj No").
                      setSize(50);
      con_doc_type_data_blk.addField("DOC_NO").
                      setMandatory().
                      setInsertable().
                      setLabel("CONDOCTYPEDATADOCNO: Doc No").
                      setSize(30);
      con_doc_type_data_blk.addField("DOC_NAME").
                      setInsertable().
                      setLabel("CONDOCTYPEDATADOCNAME: Doc Name").
                      setSize(30);
      con_doc_type_data_blk.addField("LOGIC_UNIT").
                      setInsertable().
                      setLabel("CONDOCTYPEDATALOGICUNIT: Logic Unit").
                      setSize(30);
      con_doc_type_data_blk.addField("ITEM0_NOTE").
                      setDbName("NOTE").
                      setInsertable().
                      setLabel("CONDOCTYPEDATAITEM0NOTE: Note").
                      setSize(120);
      con_doc_type_data_blk.setView("CON_DOC_TYPE_DATA");
      con_doc_type_data_blk.defineCommand("CON_DOC_TYPE_DATA_API","New__,Modify__,Remove__");
      con_doc_type_data_blk.setMasterBlock(headblk);
      con_doc_type_data_set = con_doc_type_data_blk.getASPRowSet();
      con_doc_type_data_bar = mgr.newASPCommandBar(con_doc_type_data_blk);
      con_doc_type_data_bar.defineCommand(con_doc_type_data_bar.OKFIND, "okFindITEM1");
      con_doc_type_data_bar.defineCommand(con_doc_type_data_bar.NEWROW, "newRowITEM1");
      con_doc_type_data_tbl = mgr.newASPTable(con_doc_type_data_blk);
      con_doc_type_data_tbl.setTitle("CONDOCTYPEDATAITEMHEAD1: ConDocTypeData");
      con_doc_type_data_tbl.enableRowSelect();
      con_doc_type_data_tbl.setWrap();
      con_doc_type_data_lay = con_doc_type_data_blk.getASPBlockLayout();
      con_doc_type_data_lay.setDefaultLayoutMode(con_doc_type_data_lay.MULTIROW_LAYOUT);
      con_doc_type_data_lay.setDataSpan("LOGIC_UNIT", 5);



   }



   public void  adjust()
   {
      // fill function body
   }

   //-----------------------------------------------------------------------------
   //------------------------  Presentation functions  ---------------------------
   //-----------------------------------------------------------------------------

   protected String getDescription()
   {
      return "CONDOCTYPEDATADESC: Con Doc Type Data";
   }


   protected String getTitle()
   {
      return "CONDOCTYPEDATATITLE: Con Doc Type Data";
   }


   protected void printContents() throws FndException
   {
      ASPManager mgr = getASPManager();
      if (headlay.isVisible())
          appendToHTML(headlay.show());
      else 
      {
         headlay.setLayoutMode(headlay.CUSTOM_LAYOUT);
         appendToHTML(headlay.show());
      }  
      if ((headlay.isSingleLayout() || headlay.isCustomLayout()) && headset.countRows() > 0)
      {
         appendToHTML(con_doc_type_data_lay.show());
      }
   }
}
