   public void validate() {
      // TODO Auto-generated method stub
      ASPManager mgr = getASPManager();
      ASPTransactionBuffer trans = mgr.newASPTransactionBuffer();
      ASPCommand cmd;
      ASPTransactionBuffer trans1 = mgr.newASPTransactionBuffer();
      ASPCommand cmd1;
      
      String val = mgr.readValue("VALIDATE");
      String txt = "";
      String  RESPONSE_PERSON_NAME = "";
      String  RESPONSE_DEPT_DESC = "";
      String  RESPONSE_DEPT = "";
      
      if ("RESPONSE_PERSON".equals(val)) {
         
         cmd = trans.addCustomFunction("GETRESPONSEPERSONNAME", 
               "PERSON_INFO_API.GET_NAME", "RESPONSE_PERSON_NAME");
         cmd.addParameter("RESPONSE_PERSON");
         
         cmd = trans.addCustomFunction("GETRESPONSEDEPT", 
               "PERSON_ZONE_API.Get_User_Def_Zone", "RESPONSE_DEPT");
         cmd.addParameter("RESPONSE_PERSON");
         
         trans = mgr.validate(trans);
         RESPONSE_PERSON_NAME = trans.getValue("GETRESPONSEPERSONNAME/DATA/RESPONSE_PERSON_NAME");
         RESPONSE_DEPT = trans.getValue("GETRESPONSEDEPT/DATA/RESPONSE_DEPT");
         
         
         cmd1 = trans1.addCustomFunction("GETRESPONSEDEPTDESC", 
               "GENERAL_ZONE_API.Get_Zone_Desc", "RESPONSE_DEPT_DESC");
         cmd1.addParameter("RESPONSE_DEPT",RESPONSE_DEPT);
         trans1 = mgr.validate(trans1);
         RESPONSE_DEPT_DESC = trans1.getValue("GETRESPONSEDEPTDESC/DATA/RESPONSE_DEPT_DESC");

         txt = ((mgr.isEmpty(RESPONSE_PERSON_NAME)) ? "" : RESPONSE_PERSON_NAME) + "^"
               + ((mgr.isEmpty(RESPONSE_DEPT)) ? "" : RESPONSE_DEPT) + "^"
               + ((mgr.isEmpty(RESPONSE_DEPT_DESC)) ? "" : RESPONSE_DEPT_DESC) + "^";
      }
      
      mgr.responseWrite(txt);
      mgr.endResponse();
   }
   
   
   
