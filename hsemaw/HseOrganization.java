   public void validate() {
      // TODO Auto-generated method stub
      ASPManager mgr = getASPManager();
      ASPTransactionBuffer trans = mgr.newASPTransactionBuffer();
      ASPCommand cmd;
      ASPTransactionBuffer trans1 = mgr.newASPTransactionBuffer();
      ASPCommand cmd1;
      
      String val = mgr.readValue("VALIDATE");
      String txt = "";
      String  CREATE_PERSON_NAME = "";
      String  ORG_NAME = "";
      String  ORG_NAME_DESC = "";
      String  DEPT_DESC = "";
      String  PERSON_NAME_DESC = "";
      String  DEPT = "";
      
      if ("CREATE_PERSON".equals(val)) {
         
         cmd = trans.addCustomFunction("GETCREATEPERSONNAME", 
               "PERSON_INFO_API.GET_NAME", "CREATE_PERSON_NAME");
         cmd.addParameter("CREATE_PERSON");
         
         cmd = trans.addCustomFunction("GETPERSONDEFZONE", 
               "PERSON_ZONE_API.Get_User_Def_Zone", "ORG_NAME");
         cmd.addParameter("CREATE_PERSON");
         
         trans = mgr.validate(trans);
         CREATE_PERSON_NAME = trans.getValue("GETCREATEPERSONNAME/DATA/CREATE_PERSON_NAME");
         ORG_NAME = trans.getValue("GETPERSONDEFZONE/DATA/ORG_NAME");
         
        
         cmd1 = trans1.addCustomFunction("GETPERSONORGNAME", 
               "GENERAL_ZONE_API.Get_Zone_Desc", "ORG_NAME_DESC");
         cmd1.addParameter("ORG_NAME",ORG_NAME);
         trans1 = mgr.validate(trans1);
         ORG_NAME_DESC = trans1.getValue("GETPERSONORGNAME/DATA/ORG_NAME_DESC");

         txt = ((mgr.isEmpty(CREATE_PERSON_NAME)) ? "" : CREATE_PERSON_NAME) + "^"
               + ((mgr.isEmpty(ORG_NAME)) ? "" : ORG_NAME) + "^"
               + ((mgr.isEmpty(ORG_NAME_DESC)) ? "" : ORG_NAME_DESC) + "^";
         mgr.responseWrite(txt);
      }else if("PERSON_NAME".equals(val)) {
         cmd = trans.addCustomFunction("GETPERSONNAMEDESC", 
               "PERSON_INFO_API.GET_NAME", "PERSON_NAME_DESC");
         cmd.addParameter("PERSON_NAME");
         
         cmd = trans.addCustomFunction("GETPERSONORG", 
               "PERSON_ZONE_API.Get_User_Def_Zone", "DEPT");
         cmd.addParameter("PERSON_NAME");
         
         trans = mgr.validate(trans);
         PERSON_NAME_DESC = trans.getValue("GETPERSONNAMEDESC/DATA/PERSON_NAME_DESC");
         DEPT = trans.getValue("GETPERSONORG/DATA/DEPT");
         
         
         cmd1 = trans1.addCustomFunction("GETPERSONDEPT", 
               "GENERAL_ZONE_API.Get_Zone_Desc", "DEPT_DESC");
         cmd1.addParameter("DEPT",DEPT);
         trans1 = mgr.validate(trans1);
         DEPT_DESC = trans1.getValue("GETPERSONDEPT/DATA/DEPT_DESC");

         txt = ((mgr.isEmpty(PERSON_NAME_DESC)) ? "" : PERSON_NAME_DESC) + "^"
               + ((mgr.isEmpty(DEPT)) ? "" : DEPT) + "^"
               + ((mgr.isEmpty(DEPT_DESC)) ? "" : DEPT_DESC) + "^";
         mgr.responseWrite(txt);
      }
      mgr.endResponse();
   }
   

