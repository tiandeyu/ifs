public class HseCheck extends ASPPageProvider
{

   //-----------------------------------------------------------------------------
   //---------- Static constants ------------------------------------------------
   //-----------------------------------------------------------------------------

   public static boolean DEBUG = Util.isDebugEnabled("ifs.hsemaw.HseCheck");

   //-----------------------------------------------------------------------------
   //---------- Header Instances created on page creation --------
   //-----------------------------------------------------------------------------

   private ASPBlock headblk;
   private ASPRowSet headset;
   private ASPCommandBar headbar;
   private ASPTable headtbl;
   private ASPBlockLayout headlay;

   //-----------------------------------------------------------------------------
   //------------------------  Construction  ---------------------------
   //-----------------------------------------------------------------------------

   public  HseCheck (ASPManager mgr, String page_path)
   {
      super(mgr,page_path);
   }

   public void run()
   {
      ASPManager mgr = getASPManager();

      if( mgr.commandBarActivated() )
         eval(mgr.commandBarFunction());
      else if(mgr.dataTransfered())
         okFind();
      else if( !mgr.isEmpty(mgr.getQueryStringValue("SEARCH")) )
         okFind();
      else 
         okFind();
      
      adjust();
   }
   //-----------------------------------------------------------------------------
   //------------------------  Command Bar functions  ---------------------------
   //-----------------------------------------------------------------------------

   public void okFind()
   {
      ASPManager mgr = getASPManager();
      ASPTransactionBuffer trans = mgr.newASPTransactionBuffer();
      ASPQuery q;

      mgr.createSearchURL(headblk);
      q = trans.addQuery(headblk);
      q.includeMeta("ALL");
      if(mgr.dataTransfered())
         q.addOrCondition(mgr.getTransferedData());
      mgr.querySubmit(trans,headblk);
      if (  headset.countRows() == 0 )
      {
         mgr.showAlert("HSECHECKNODATA: No data found.");
         headset.clear();
      }
   }



   public void countFind()
   {
      ASPManager mgr = getASPManager();
      ASPTransactionBuffer trans = mgr.newASPTransactionBuffer();
      ASPQuery q;

      q = trans.addQuery(headblk);
      q.setSelectList("to_char(count(*)) N");
      mgr.submit(trans);
      headlay.setCountValue(toInt(headset.getValue("N")));
      headset.clear();
   }



   public void newRow()
   {
      ASPManager mgr = getASPManager();
      ASPTransactionBuffer trans = mgr.newASPTransactionBuffer();
      ASPBuffer data;
      ASPCommand cmd;

      cmd = trans.addEmptyCommand("HEAD","HSE_CHECK_API.New__",headblk);
      cmd.setOption("ACTION","PREPARE");
      trans = mgr.perform(trans);
      data = trans.getBuffer("HEAD/DATA");
      headset.addRow(data);
   }


   //-----------------------------------------------------------------------------
   //------------------------  Predefines Head ---------------------------
   //-----------------------------------------------------------------------------

   public void  preDefine()
   {
      ASPManager mgr = getASPManager();

      headblk = mgr.newASPBlock("MAIN");
      headblk.addField("OBJID").
              setHidden();
      headblk.addField("OBJVERSION").
              setHidden();
      headblk.addField("PROJ_NO").
              setMandatory().
              setInsertable().
              setDynamicLOV("GENERAL_PROJECT").
              setLabel("HSECHECKPROJNO: Proj No").
              setSize(30);
      headblk.addField("GENERAL_PROJECT_PROJ_DESC").
              setFunction("GENERAL_PROJECT_API.GET_PROJ_DESC (:PROJ_NO)").
              setLabel("HSECHECKGENERALPROJECTPROJDESC: General Project Proj Desc").
              setSize(20).
              setReadOnly();
      
      mgr.getASPField("PROJ_NO").setValidation("GENERAL_PROJECT_PROJ_DESC");
      headblk.addField("ID").
              setHidden().
              setLabel("HSECHECKID: Id").
              setSize(30);
      headblk.addField("CHECK_CONTENT").
              setInsertable().
              setLabel("HSECHECKCHECKCONTENT: Check Content").
              setSize(30);
      headblk.addField("CHECK_ORG").
              setInsertable().
              setDynamicLOV("GENERAL_ZONE").
//              setLOVProperty("WHERE", "PERSON_ID = '"+mgr.getFndUser()+"'").
              setLabel("HSECHECKCHECKORG: Check Org").
              setSize(30);
      headblk.addField("CHECK_ORG_DESC").
              setReadOnly().
              setFunction("GENERAL_ORGANIZATION_API.Get_Org_Desc_ (:CHECK_ORG)").
              setLabel("HSECHECKCHECKORGDESC: Check Org Desc").
              setSize(30);  
      mgr.getASPField("CHECK_ORG").setValidation("CHECK_ORG_DESC");
      
      headblk.addField("CHECK_TIME","Date").
              setInsertable().
              setLabel("HSECHECKCHECKTIME: Check Time").
              setSize(30);
      headblk.addField("RESPONSE_PERSON").
              setInsertable().
              setDynamicLOV("PERSON_INFO").
              setLabel("HSECHECKRESPONSEPERSON: Response Person").
              setSize(30);
      headblk.addField("RESPONSE_PERSON_NAME").
              setFunction("PERSON_INFO_API.GET_NAME ( :RESPONSE_PERSON)").
              setLabel("HSECHECKRESPONSEPERSONNAME: Response Person Name").
              setSize(30).
              setReadOnly();
      mgr.getASPField("RESPONSE_PERSON").setValidation("RESPONSE_PERSON_NAME");
      
      headblk.addField("AMOUNT","Number").
              setInsertable().
              setLabel("HSECHECKAMOUNT: Amount").
              setSize(30);
      headblk.addField("APPRAISE").
              setInsertable().
              setLabel("HSECHECKAPPRAISE: Appraise").
              setSize(120).
              setHeight(5);
      headblk.addField("CHANGE_MEASURE").
              setInsertable().
              setLabel("HSECHECKCHANGEMEASURE: Change Measure").
              setSize(120).
              setHeight(5);
      headblk.addField("AWARD_OR_PUNISH_INFO").
              setInsertable().
              setLabel("HSECHECKAWARDORPUNISHINFO: Award Or Punish Info").
              setSize(120).
              setHeight(5);
      
      headblk.addField("NOTE").
              setInsertable().
              setLabel("HSECHECKNOTE: Note").
              setSize(120).
              setHeight(5);
      
      headblk.setView("HSE_CHECK");
      headblk.defineCommand("HSE_CHECK_API","New__,Modify__,Remove__");
      headset = headblk.getASPRowSet();
      headbar = mgr.newASPCommandBar(headblk);
      headtbl = mgr.newASPTable(headblk);
      headtbl.setTitle("HSECHECKTBLHEAD: Hse Checks");
      headtbl.enableRowSelect();
      headtbl.setWrap();
      headlay = headblk.getASPBlockLayout();
      headlay.setDataSpan("NOTE", 5);
      headlay.setDataSpan("CHANGE_MEASURE", 5);
      headlay.setDataSpan("AWARD_OR_PUNISH_INFO", 5);
      headlay.setDataSpan("APPRAISE", 5);
      headlay.setSimple("GENERAL_PROJECT_PROJ_DESC");
      headlay.setDefaultLayoutMode(headlay.MULTIROW_LAYOUT);
      headlay.setSimple("CHECK_ORG_DESC");
      headlay.setSimple("RESPONSE_PERSON_NAME");
      

   }



   public void  adjust()
   {
      // fill function body
   }

   //-----------------------------------------------------------------------------
   //------------------------  Presentation functions  ---------------------------
   //-----------------------------------------------------------------------------

   protected String getDescription()
   {
      return "HSECHECKDESC: Hse Check";
   }


   protected String getTitle()
   {
      return "HSECHECKTITLE: Hse Check";
   }


   protected void printContents() throws FndException
   {
      ASPManager mgr = getASPManager();
      if (headlay.isVisible())
          appendToHTML(headlay.show());

   }
}
