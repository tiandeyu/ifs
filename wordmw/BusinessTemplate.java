//-----------------------------------------------------------------------------
//-----------------------------   Class def  ------------------------------
//-----------------------------------------------------------------------------

public class BusinessTemplate extends ASPPageProvider
{

   //-----------------------------------------------------------------------------
   //---------- Static constants ------------------------------------------------
   //-----------------------------------------------------------------------------

   public static boolean DEBUG = Util.isDebugEnabled("ifs.wordmw.BusinessTemplate");

   //-----------------------------------------------------------------------------
   //---------- Header Instances created on page creation --------
   //-----------------------------------------------------------------------------

   private ASPBlock headblk;
   private ASPRowSet headset;
   private ASPCommandBar headbar;
   private ASPTable headtbl;
   private ASPBlockLayout headlay;

   //-----------------------------------------------------------------------------
   //------------------------  Construction  ---------------------------
   //-----------------------------------------------------------------------------

   public  BusinessTemplate (ASPManager mgr, String page_path)
   {
      super(mgr,page_path);
   }

   public void run()
   {
      ASPManager mgr = getASPManager();

      if( mgr.commandBarActivated() )
         eval(mgr.commandBarFunction());
      else if(mgr.dataTransfered())
         okFind();
      else if (!mgr.isEmpty(mgr.getQueryStringValue("VALIDATE")) )
    	 validate();
      else if( !mgr.isEmpty(mgr.getQueryStringValue("SEARCH")) )
         okFind();
      else if( !mgr.isEmpty(mgr.getQueryStringValue("BUSS_ID")) )
         okFind();
      else
      	okFind();
      adjust();
   }
   
   
   public void validate(){
	   ASPManager mgr = getASPManager();
	   ASPTransactionBuffer trans = mgr.newASPTransactionBuffer();
	   String val = mgr.readValue("VALIDATE");
	   if("TEMPLATEID".equals(val)){
		   ASPCommand cmd = trans.addCustomFunction("TEMPLATE_NAME_","GD_TEMPLATE_FILE_API.Get_Desc_By_Record_Id","TEMPLATE_NAME_");
           cmd.addParameter("TEMPLATEID");
           trans = mgr.validate(trans);
           String result = trans.getValue("TEMPLATE_NAME_/DATA/TEMPLATE_NAME_");
          if(mgr.isEmpty(result)){
        	  mgr.responseWrite(result+ "^");
          }else{
        	  mgr.responseWrite(result+ "^");
          }
	   }
	   
	   mgr.endResponse();
   }
   //-----------------------------------------------------------------------------
   //------------------------  Command Bar functions  ---------------------------
   //-----------------------------------------------------------------------------

   public void okFind()
   {
      ASPManager mgr = getASPManager();
      ASPTransactionBuffer trans = mgr.newASPTransactionBuffer();
      ASPQuery q;

      mgr.createSearchURL(headblk);
      q = trans.addQuery(headblk);
      q.includeMeta("ALL");
      if(mgr.dataTransfered())
         q.addOrCondition(mgr.getTransferedData());
      mgr.querySubmit(trans,headblk);
      if (  headset.countRows() == 0 )
      {
         mgr.showAlert("BUSINESSTEMPLATENODATA: No data found.");
         headset.clear();
      }
   }



   public void countFind()
   {
      ASPManager mgr = getASPManager();
      ASPTransactionBuffer trans = mgr.newASPTransactionBuffer();
      ASPQuery q;

      q = trans.addQuery(headblk);
      q.setSelectList("to_char(count(*)) N");
      mgr.submit(trans);
      headlay.setCountValue(toInt(headset.getValue("N")));
      headset.clear();
   }



   public void newRow()
   {
      ASPManager mgr = getASPManager();
      ASPTransactionBuffer trans = mgr.newASPTransactionBuffer();
      ASPBuffer data;
      ASPCommand cmd;

      cmd = trans.addEmptyCommand("HEAD","BUSINESS_TEMPLATE_API.New__",headblk);
      cmd.setOption("ACTION","PREPARE");
      trans = mgr.perform(trans);
      data = trans.getBuffer("HEAD/DATA");
      headset.addRow(data);
   }


   //-----------------------------------------------------------------------------
   //------------------------  Predefines Head ---------------------------
   //-----------------------------------------------------------------------------

   public void  preDefine()
   {
      ASPManager mgr = getASPManager();

      headblk = mgr.newASPBlock("MAIN");
      headblk.addField("OBJID").
              setHidden();
      headblk.addField("OBJVERSION").
              setHidden();
      headblk.addField("BUSS_ID","Number").
              //setMandatory().
              //setInsertable().
              setReadOnly().
              setLabel("BUSINESSTEMPLATEBUSSID: Buss Id").
              setSize(10);
      headblk.addField("PAGE_NAME").
              setInsertable().
              setLabel("BUSINESSTEMPLATEPAGENAME: Page Name").
              setSize(40);
      headblk.addField("PAGE_URL").
              setInsertable().
              setLabel("BUSINESSTEMPLATEPAGEURL: Page Url").
              setSize(60);
      headblk.addField("TEMPLATEID").
              setInsertable().
              setDynamicLOV("TEMPLATE_FILE_LOV").
              setLabel("BUSINESSTEMPLATETEMPLATEID: Templateid").
              setSize(20).setCustomValidation("TEMPLATEID", "TEMPLATE_NAME_");
      headblk.addField("TEMPLATE_NAME_").
              //setInsertable().
              setReadOnly().
              //setFunction("GD_TEMPLATE_FILE_API.Get_Descript(:TEMPLATEID)").
              setLabel("BUSINESSTEMPLATETEMPLATENAME: Template Name ").
              setSize(50);
      //mgr.getASPField("TEMPLATEID").setValidation("TEMPLATE_NAME_");
      headblk.addField("IS_USE").
              setInsertable().
              setCheckBox("FALSE,TRUE").
              setLabel("BUSINESSTEMPLATEISUSE: Is Use").
              setSize(30);
      headblk.setView("BUSINESS_TEMPLATE");
      headblk.defineCommand("BUSINESS_TEMPLATE_API","New__,Modify__,Remove__");
      headset = headblk.getASPRowSet();
      headbar = mgr.newASPCommandBar(headblk);
      headtbl = mgr.newASPTable(headblk);
      headtbl.setTitle("BUSINESSTEMPLATETBLHEAD: Business Templates");
      headtbl.enableRowSelect();
      headtbl.setWrap();
      headlay = headblk.getASPBlockLayout();
      headlay.setDefaultLayoutMode(headlay.MULTIROW_LAYOUT);
 



   }



   public void  adjust()
   {
      // fill function body
   }

   //-----------------------------------------------------------------------------
   //------------------------  Presentation functions  ---------------------------
   //-----------------------------------------------------------------------------

   protected String getDescription()
   {
      return "BUSINESSTEMPLATEDESC: Business Template";
   }


   protected String getTitle()
   {
      return "BUSINESSTEMPLATETITLE: Business Template";
   }


   protected void printContents() throws FndException
   {
      ASPManager mgr = getASPManager();
      if (headlay.isVisible())
          appendToHTML(headlay.show());

   }
}
